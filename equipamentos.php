<section class="content-section" id="descricao-das-armas">
<h2>Descrição das Armas</h2>
<p><strong>Adaga.</strong> Uma faca afiada comumente utilizada por muitos habitantes dos reinos. A adaga é uma arma arremessável, discreta e ligeira.</p>
<p><strong>Alabarda.</strong> Uma haste de madeira com 2 metros de comprimento, possuindo uma lâmina de machado na ponta. A alabarda é uma arma alongada.</p>
<p><strong>Alfanje.</strong> Uma variante maior da cimitarra, caracterizada por uma lâmina larga e curva. Amplamente empregada por guardas de palácios e combatentes de infantaria.</p>
<p><strong>Arco Curto.</strong> Uma arma antiga e comum, frequentemente usada como ferramenta de caça. Também é adotada por milicianos, bandidos e exércitos menos equipados. Pode ser utilizado montado.</p>
<p><strong>Arco Longo.</strong> Este arco reforçado tem a altura de uma pessoa e é, principalmente, uma arma de guerra. Devido à sua resistência, permite que você aplique sua Força nas rolagens de dano, ao contrário de outras armas de disparo. Entretanto, não é adequado para uso montado.</p>
<p><strong>Arco de Guerra.</strong> Um arco robusto com braços grossos e poderosos, capaz de disparos potentes. Semelhante ao arco longo, permite a aplicação da Força nas rolagens de dano e não pode ser usado montado. A exigência de força para tensionar o arco o torna uma arma desbalanceada.</p>
<p><strong>Arpão.</strong> Inicialmente utilizado como instrumento de pesca, esta haste de madeira possui uma ponta metálica com rebarbas em uma extremidade e uma corda na outra. Se acertar o alvo, o arpão fica preso, permitindo que você faça testes de Força opostos contra a vítima quando ela tenta se mover. A vítima pode se soltar gastando uma ação de movimento, sofrendo 1d10 pontos de dano de perfuração.</p>
<p><strong>Azagaia.</strong> Uma lança leve e flexível, especialmente projetada para arremessos. Pode ser usada como arma corpo a corpo, mas impõe uma penalidade de -5 no teste de ataque.</p>
<p><strong>Bacamarte.</strong> Esta arma de fogo foi projetada para atingir múltiplos alvos. Faça um teste de ataque e compare o resultado contra a Defesa de todas as criaturas em um cone de 6m. Aqueles atingidos sofrem pontos de dano. Recarregar um bacamarte exige uma ação completa e 2 munições.</p>
<p><strong>Besta Leve.</strong> Um arco montado sobre uma coronha de madeira com um gatilho, a arma leve de besta é um dispositivo que dispara virotes com considerável potência. Recarregar uma arma leve de besta requer uma ação de movimento.</p>
<p><strong>Besta Pesada.</strong> Uma versão maior e mais potente da arma leve de besta. Recarregar uma arma pesada de besta demanda uma ação padrão.</p>
<p><strong>Bico de Corvo.</strong> Uma haste de madeira com 2 metros de comprimento, terminando em uma lâmina metálica, semelhante a uma lança. Na base desta lâmina, encontra-se um gancho afiado de um lado e uma cabeça de martelo do outro. O bico de corvo é uma arma alongada e versátil, proporcionando um bônus de +2 em testes para derrubar e quebrar.</p>
<p><strong>Bordão.</strong> Um cajado valorizado por viajantes e camponeses, o bordão é uma arma dupla.</p>
<p><strong>Chicote.</strong> Esta arma pode ser usada para atacar inimigos em um alcance de até 4,5 metros e pode se enroscar nas mãos, pernas ou armas dos adversários. O chicote é uma arma ágil e versátil, fornecendo um bônus de +2 em testes para derrubar ou desarmar.</p>
<p><strong>Cimitarra.</strong> Uma espada com lâmina curva e extremamente afiada, a cimitarra é uma arma ágil.</p>
<p><strong>Clava.</strong> Um pedaço de madeira empunhado como arma, frequentemente usado por bárbaros ou criaturas brutais, ou como uma arma improvisada, como um galho de árvore ou pedaço de mobília. Sendo fácil de adquirir, seu preço é zero.</p>
<p><strong>Corrente de Espinhos.</strong> Esta arma pode ser usada para atacar inimigos em um alcance de até 4,5 metros e pode se enroscar nas mãos, pernas ou armas dos adversários. A corrente de espinhos é uma arma ágil, dupla e versátil, proporcionando um bônus de +2 em testes para derrubar ou desarmar.</p>
<p><strong>Espada Bastarda.</strong> Maior e mais pesada que a espada longa, esta arma é tradicionalmente utilizada por cavaleiros. A espada bastarda é uma arma adaptável. É muito grande para ser usada com uma só mão sem treinamento especial, sendo assim uma arma exótica. Pode ser empregada como uma arma marcial de duas mãos.</p>
<p><strong>Espada Curta.</strong> Uma espada comum, com um comprimento entre 40 e 50 centímetros. Utilizada por guardas, bandidos ou como uma arma secundária por guerreiros mais habilidosos.</p>
<p><strong>Espada Larga.</strong> Utilizada por mercenários e bandidos, esta arma possui uma lâmina menos afiada, porém mais larga e pesada do que a espada longa.</p>
<p><strong>Espada Longa.</strong> Arma comum entre soldados e guerreiros, esta espada de dois gumes possui uma lâmina reta com medidas entre 80cm e 1m.</p>
<p><strong>Florete.</strong> A lâmina leve e fina desta espada proporciona grande precisão. O florete é uma arma ágil.</p>
<p><strong>Foice.</strong> Inicialmente uma ferramenta agrícola, consiste em uma lâmina curva presa a um cabo de madeira. É uma arma tradicionalmente utilizada por xamãs.</p>
<p><strong>Funda.</strong> Uma tira de couro utilizada para arremessar pedras polidas. Pode disparar pedras comuns, mas o dano é reduzido em um passo. Recarregar uma funda é uma ação de movimento. Você aplica sua Força às rolagens de dano com uma funda.</p>
<p><strong>Gadanho.</strong> Outra ferramenta agrícola, o gadanho é uma versão maior da foice, projetada para ser utilizada com as duas mãos. Criada inicialmente para ceifar cereais, também é eficaz em ceifar vidas.</p>
<p><strong>Gancho.</strong> Um gancho curvo de metal com uma empunhadura perpendicular, utilizado para puxar fardos de feno nos campos, içar cargas em embarcações e, se necessário, infligir ferimentos fatais.</p>
<p><strong>Lança.</strong> Qualquer arma feita com uma haste de madeira e uma ponta afiada, natural ou metálica. Devido à sua facilidade de fabricação, é comum entre povos me nos civilizados. Pode ser arremessável.</p>
<p><strong>Lança Montada.</strong> Uma lança montada é uma arma alongada. Se você estiver montado, pode usá-la com apenas uma mão. Além disso, ao ser utilizada numa investida montada, causa +2d8 pontos de dano (não multipli cados em caso de acerto crítico).</p>
<p><strong>Maça.</strong> Um bastão com um peso cheio de protuberâncias na ponta, a maça é usada por clérigos que fazem votos de não derramar sangue. Um golpe de maça pode não derramar sangue, mas esmaga ossos.</p>
<p><strong>Maça de Guerra.</strong> Uma versão mais ameaçadora da maça comum, apresentando grandes placas de metal na ponta. O peso considerável da maça de guerra confere grande poder aos seus golpes, mas também a torna uma arma desbalanceada. Devido ao seu peso, exige treinamento especial para ser manejada, sendo assim, considerada uma arma exótica.</p>
<p><strong>Maça-estrela.</strong> Uma variação da maça com cravos, esta arma requer maior habilidade no manuseio, mas compensa desferindo golpes mais perigosos.</p>
<p><strong>Machadinha.</strong> Ferramenta útil para cortar madeira e, incidentalmente, inimigos. Possui a capacidade de ser arremessada.</p>
<p><strong>Machado Duplo.</strong> A preferência de onze entre dez guerreiros anões. O machado duplo é demasiadamente grande para ser utilizado com uma só mão sem treinamento especial, classificando-se como uma arma exótica. Pode ser empregado como uma arma marcial de duas mãos.</p>
<p><strong>Machado de Batalha.</strong> Adaptado do machado de lenhador, esta não é uma ferramenta para a derrubada de árvores, mas sim uma arma capaz de infligir ferimentos terríveis.</p>
<p><strong>Machado de Guerra.</strong> Este imponente machado de lâmina dupla é uma das armas mais temíveis existentes.</p>
<p><strong>Marrão.</strong> Uma versão mais pesada da marreta de guerra, esta arma ostenta uma cabeça de metal capaz de fraturar pedra, aço e ossos. Devido ao seu tamanho, requer treinamento especial para ser utilizada, sendo, portanto, uma arma exótica e desbalanceada devido ao seu peso.</p>
<p><strong>Mangual.</strong> Uma haste metálica conectada a uma corrente, com uma esfera de aço na ponta, capaz de se enroscar na arma do adversário. O mangual é uma arma versátil, fornecendo um bônus de +2 em testes para desarmar.</p>
<p><strong>Malho.</strong> Ferramenta de construção, este martelo possui uma cabeça de madeira dura e pesada, ideal para enterrar estacas no solo. Embora não seja tão eficiente quanto martelos e marretas projetados para a guerra, seu peso excessivo pode desequilibrar os oponentes. O malho é uma arma versátil, conferindo um bônus de +2 em testes para derrubar ou empurrar.</p>
<p><strong>Marreta de Guerra.</strong> Composta por uma haste de madeira robusta e uma cabeça de metal ou pedra, esta arma sacrifica precisão em troca de golpes potentes.</p>
<p><strong>Martelo de Batalha.</strong> Outra ferramenta adaptada para o combate, este é o favorito de quase todos os anões que não usam machados.</p>
<p><strong>Martelo Longo.</strong> Uma haste de madeira com 2 metros de comprimento, esta arma possui uma ponta de metal afiada ao lado de uma cabeça densa e pesada. Combinando o alcance de uma alabarda com a contundência de um martelo, é uma arma alongada.</p>
<p><strong>Montante.</strong> Enorme e pesada, esta espada de 1,5 metros de comprimento é uma arma poderosa.</p>
<p><strong>Mosquete.</strong> Uma arma de fogo de manuseio desafiador, porém, com poder devastador. Executar a recarga de um mosquete requer uma ação padrão.</p>
<p><strong>Picareta.</strong> Usada por mineradores, esta ferramenta quebra pedras com facilidade. Imagine o que pode fazer com carne e osso!</p>
<p><strong>Pique.</strong> Essencialmente uma lança muito longa, o pique é uma arma alongada.</p>
<p><strong>Pistola.</strong> A mais comum entre as armas de fogo. Realizar a recarga de uma pistola demanda uma ação padrão.</p>
<p><strong>Punhal.</strong> Esta lâmina fina é usada por cortesãos, aristocratas e outros indivíduos que supostamente não deveriam portar armas. Um punhal é uma arma discreta e ligeira. Uma vez por cena, se você possuir o poder Saque Rápido, sacar um punhal e usá-lo para atacar no mesmo turno faz com que o oponente fique desprevenido contra esse ataque.</p>
<p><strong>Rapieira.</strong> A lâmina fina e alongada desta espada de um gume a torna mais precisa, mas exige um treinamento especial para ser manuseada de forma equilibrada. A rapieira é uma arma ágil.</p>
<p>Rede. A rede tem pequenos dentes em sua trama e uma corda para controlar os inimigos presos. Se você acertar um ataque com a rede, não causa dano. Em vez disso, a vítima fica enredada (deslocamento reduzido à metade, não pode correr nem fazer investidas e sofre -2 na Defesa e em testes de ataque). Enquanto você estiver segurando a corda, sempre que a vítima se mover, você pode fazer um teste de Força oposto contra ela como uma reação. Se você vencer, a vítima só pode se mover até o limite da corda (9m). A vítima pode se soltar com uma ação completa e um teste de Força ou Acrobacia (CD 20). A rede tem 5 pontos de vida e, se rasgar, qualquer criatura enredada se solta automaticamente. A rede só pode ser usada contra criaturas no máximo uma categoria de tamanho maior que você.</p>
<p><strong>Tacape.</strong> Versão maior e/ou com pregos de uma clava. Não é uma arma elegante, mas faz o serviço.</p>
<p><strong>Tridente.</strong> Uma lança com três pontas, muito usada por pescadores costeiros e gladiadores, própria para prender as pernas do oponente. O tridente é uma arma versátil, fornecendo +2 em testes para derrubar.</p>
</section>

<h2>Esotéricos</h2>
<p><strong>Objetos usados por conjuradores para lançar magias com mais eficácia.</strong> Para usar um esotérico, você precisa segurá-lo com a mão que fará o gesto da magia. Uma magia só pode se beneficiar de um esotérico por vez. A CD para criar qualquer esotérico é 20 e para criá-lo você deve ter treinamento em Misticismo.</p>
<p><strong>Amuleto de Fé.</strong> Um amuleto com um sinal sagrado de seu deus, um colar de contas, missangas ou patuá. Ao tê-lo em uma das mãos, a CD para resistir a suas magias divinas sobe em +1.</p>
<p><strong>Apanhador de Sonhos.</strong> Um amuleto com a forma de um círculo com uma teia de aranha no centro, e uma pena pendurada na parte inferior. Quando você recebe +2 nos testes de resistência contra magias de encantamento.</p>
<p><strong>Astrolábio.</strong> Um cilindro de metal fino e preciso, usado para medir distâncias e ângulos. Quando lança uma magia que atinge uma área, você pode gastar +1 PM para ligar este item e dobrar a área de efeito da magia.</p>
<p><strong>Bolsa de Pó.</strong> Uma bolsa com pó colorido, feito de flores raras do Grande Verde. Ao lançar uma magia de ilusão ou encantamento, você ganha +2 PM para aprimoramentos.</p>
<p><strong>Cajado Arcano.</strong> Um cajado comum, de madeira boa e runas. O máximo de PM que você pode usar em magias arcanas e a CD para resistir a elas sobem em +1. Para ter seus benefícios, o cajado deve ser segurado com as duas mãos. O cajado ainda pode ser usado como arma, como se fosse um bordão.</p>
<p><strong>Carranca.</strong> Obra prima talhada em madeira, com tamanho variável com traços da cabeça de uma besta selvagem e terrível, com grandes dentes pontiagudos e uma expressão de pura fúria. Com um comando, você pode liberar o poder da Carranca e fazer os mortos-vivos tremerem de medo. Gastando uma ação padrão e 3 PM, você pode expulsar todos os mortos-vivos em alcance curto, que ficam apavorados por 1d6 rodadas.</p>
<p><strong>Cetro Elemental.</strong> Este cetro tem uma joia na ponta: esmeralda (ácido), topázio (eletricidade), rubi (fogo) ou safira (frio). Ao lançar uma magia que cause dano do mesmo tipo da joia, o dano sobe em um dado igual.</p>
<p><strong>Costela do Corpo-seco.</strong> Uma varinha de osso de morto-vivo. Suas magias causam +1d6 pontos de dano de trevas. Se estiver com esta varinha, você não recupera PV por magia de cura.</p>
<p><strong>Dedo de Ossain.</strong> Uma varinha de madeira de uma mata intocada com folhas brotando de seu cabo. Toda vez que lançar uma magia, role 1d4. Com um resultado 4, você ganha 1 PM.</p>
<p><strong>Espinho de Tucum.</strong> Uma varinha de mágica feitas com espinhos da Tucum. Suas magias causam +1d6 pontos de dano igual. Mas, toda vez que lançar uma magia, você perde 1 PV.</p>
<p><strong>Iruquerê.</strong> Trata-se de uma varinha confeccionada a partir de pelos do rabo de touro ou búfalo, fixados a um fragmento de couro rígido, formando um cabo revestido por couro fino e decorado com contas. Permite que você lance a magia Conjurar Mortos-Vivos. Caso já conheça a magia, o custo para lança-la diminui em -1 PM.</p>
<p><strong>Jogo de Búzios.</strong> Essas conchas são utilizadas como material de ritual para a magia Augúrio. Diferente dos outros materiais utilizados em rituais, o jogo de búzios não é gasto podendo ser reutilizado sempre. Para utiliza-lo é necessário um teste de misticismo CD 13.</p>
<p><strong>Luva de Ferro.</strong> Uma luva de metal com dedais ligados por correntes. Suas magias arcanas pessoais que dão bônus na Defesa ou em testes de resistência têm esse bônus aumentado em +1.</p>
<p><strong>Medalhão de Prata.</strong> Um amuleto gravado com uma runa pessoal do conjurador, este medalhão de prata reduz em –1 PM o custo de magias de alcance pessoal.</p>
<p><strong>Orbe Cristalina.</strong> Uma bola de cristal que guarda poder mágico. O máximo de PM que você pode usar em magias arcanas sobe em +1.</p>
<p><strong>Tomo Hermético.</strong> Um livro de textos que expandem o conhecimento sobre uma escola de magia. A CD para resistir a suas magias arcanas dessa escola aumenta em +2.</p>
<p><strong>Varinha Arcana.</strong> Uma varinha convencional, feita de madeira de boa qualidade e entalhada com runas. A CD para resistir a suas magias arcanas aumenta em +1.</p>

<h2>Veículos</h2>
<p><strong>Canoa.</strong> Construída a partir de um único tronco de árvore, é a embarcação mais simples. Possui as mesmas estatísticas de uma carroça, mas com deslocamento de natação.</p>
<p><strong>Caravelas.</strong> feitas de madeira, com velas triangulares ou quadradas, e têm um casco alongado e estreito. Elas são capazes de navegar contra o vento e em águas rasas, o que as torna ideais para explorar novas terras e rotas comerciais. De tamanho enorme, possui deslocamento aquático de 18m, Defesa 13 (+ Des do piloto), 400 PV e tem capacidade de transportar 40 criaturas médias (sendo 20 da tripulação).</p>
<p><strong>Carroça.</strong> Um veículo de duas ou quatro rodas, aberto e geralmente utilizado para transportar cargas pesadas. Puxada por dois cavalos ou um touro, inclui os arreios necessários para controlar os animais. Com tamanho Grande, desloca-se a 9m, possui Defesa 8 (+ Des do condutor), 50 PV e tem capacidade para transportar até 4 criaturas Médias ou 80 espaços.</p>
<p><strong>Carruagem.</strong> Uma carruagem de quatro rodas, com capacidade para até quatro pessoas na cabine fechada e mais dois condutores do lado de fora. Puxada por dois cavalos ou um touro, inclui os arreios necessários para controlar os animais. Possui as mesmas estatísticas de uma carroça, mas proporciona cobertura leve aos seus passageiros.</p>
<p>Dirigíveis Dummont. Os dirigíveis Dummont são as mais belas e avançadas máquinas voadoras de Pindorama. Criados pelo genial inventor Alberto Santos Dumont, eles são feitos de metal e vidro, com formas aerodinâmicas e elegantes. Eles usam hélices movidas a vapor para se deslocar pelo céu, com velocidade e precisão. De tamanho Enorme, possui deslocamento de voo de 20m, Defesa 10 (+ Des do piloto), 250 PV e tem capacidade para transportar até 30 criaturas Médias (sendo 10 da tripulação) ou 200 espaços. Contrariando as expectativas, quedas de dirigíveis raramente resultam em fatalidades. Quando o dirigível perde mais da metade de seus PV, inicia uma lenta descida, causando 4d6 pontos de dano de impacto a cada ocupante (Ref CD 15 para reduzir pela metade). Um dirigível só cai de maneira perigosa se perder todos os seus PV, nesse caso, os ocupantes sofrem o dano normal da queda, dependendo da altura. Realizar reparos em um dirigível em pleno voo exige uma ação completa e um teste de Ofício (artesão) contra CD 15. Em caso de sucesso, recupera-se 1d8 PV do dirigível.</p>
<p>Navio de Guerra. Uma embarcação poderosa e temida, usada para combater em alto-mar, rios e lagos. O navio de guerra é feito de madeira resistente, reforçada com metal e couro. Possui um mastro principal com uma vela quadrada, um leme na popa e armamentos pesados, composto por canhões, catapultas, balistas e bestas. Ele pode disparar projéteis de ferro, pedra, fogo ou magia contra os inimigos, causando grandes estragos. De tamanho enorme, possui deslocamento aquático de 20m, Defesa 15 (+ Des do piloto), 600 PV e tem capacidade de transportar 60 criaturas médias (sendo 30 da tripulação).</p>
<p>Pau-de-arara. Uma alternativa mais acessível de viagens aéreas, o feita de couro e trapos, o Pau-de-arara, o transporte mais comuns entre os candangos, desafia as leis da natureza e do “bom senso”. Sua gôndola, um cesto enorme cheio de bugigangas, abriga até 8 passageiros ou 160 cargas. O balão tem tamanho Enorme, voo 12m, Defesa 5 (+ Des do baloeiro), 100 PV. Mas não se engane: ele não é indestrutível. Se perder metade de seus PV, o ar quente escapa e ele desce devagar até o chão. Cada um a bordo sofre 4d6 pontos de dano de impacto (Ref CD 15 reduz à metade). Se perder todos os PV, aí sim é o fim. O balão explode e cai em chamas, causando dano normal pela queda aos ocupantes. Para evitar isso, é preciso consertar o balão em pleno ar. Isso requer uma ação completa e um teste de Ofício (artesão) contra CD 15. Se passar, recupera 1d8 PV do balão.</p>

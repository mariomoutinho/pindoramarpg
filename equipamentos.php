<p><strong>Alforje.</strong> Bolsas de couro projetadas para serem fixadas em uma sela. Permitem que um parceiro de montaria transporte até 10 espaços de itens para você.</p>
<p><strong>Cão de Caça.</strong> Este cão corajoso e leal pode servir como parceiro perseguidor ou de montaria para personagens Pequenos.</p>

<p><strong>Saco de Sal.</strong> Um pequeno saco de couro com sal marinho que proporciona +2 à CD de magias de abjuração.</p>
<p><strong>Seixo de Âmbar.</strong> Uma "gema" feita de seiva de árvore fossilizada que reduz o custo de magias de transmutação em –1 PM.</p>

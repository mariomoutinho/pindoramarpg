<section class="content-section" id="riqueza-moedas">
<h2>Riqueza &amp; Moedas</h2>
<p><strong>Em todo o vasto território de Pindorama, a unidade monetária vigente é o Marê (M$).</strong> Esta moeda consiste em uma peça circular de prata, medindo 1,5 cm de diâmetro, adornada em um lado pelo semblante de Kiantomerê, o Deus das Riquezas. Kiantomerê é representado como um unhabá com plumas enfeitando sua cabeça e uma serpente deitada em seu ombro. No lado oposto, a imagem é a mesma, mas com uma unhabá feminina.</p>
<p>A origem do Merê remonta à grandiosa Batalha da Liberdade, na qual Lil Nácio restituiu parte das terras de Pindorama ao seu povo nativo e aos unhabás que combateram ao seu lado. Com o advento de um novo governo, instituiu-se a nova moeda, reduzindo a influência do Real (R$), que até então predominava em Pindorama, aos Reinos do Império do Brasil. Atualmente, em meio a um clima de desconfiança, onde a iminência de uma guerra paira, a conversão entre as duas moedas torna-se praticamente inviável, assim como o diálogo entre esses distintos povos.</p>
<p>No Império do Brasil, a moeda oficial é o Real (R$), com a efígie de Sir Manoellius, "O Venturoso", rei dos povos caucazis lusinos que ordenou a invasão a Pindorama, estampada em um dos lados, enquanto o outro exibe sua coroa.</p>
<p><strong>É importante salientar que nem todos os reinos de Pindorama adotam o padrão do Marê.</strong> Em algumas regiões, devido à escassez de prata, utiliza-se o Marê de cobre como moeda padrão. Em outros lugares, o valor do Marê pode sofrer flutuações, causando tumultos entre comerciantes, falsificadores e piratas contrabandistas.</p>
<p>Apesar de os preços de bens e serviços serem geralmente expressos em M$, existem duas variantes da moeda: o Marê de cobre (MC), equivalente a um décimo do M$, utilizado por camponeses e plebeus; e o Marê de Ouro (TO), valendo M$ 10, destinado a aristocratas, grandes mercadores e aventureiros de destaque.</p>
<p>Se a aventura ou campanha se desenrolar no Império do Brasil, é possível ajustar os preços para o R$ da mesma maneira que foi descrito anteriormente com o Marê. Nesse caso, o Real de Cobre (RC$) valerá um décimo do R$, enquanto o Real de Ouro (RO$) equivalerá a R$ 10.</p>
</section>
<section class="content-section" id="troca-comercio">
<h2>Troca &amp; Comércio</h2>
<p><strong>Em Pindorama, a atividade comercial é gerenciada por governos locais.</strong> Essas representantes são responsáveis por determinar quem tem permissão para vender o quê e estabelecer os valores dos produtos e serviços que estão sob seu controle, assegurando assim um padrão nos preços. Contudo, em regiões distantes, ermas ou sob o domínio de governantes tirânicos, os preços podem apresentar consideráveis variações.</p>
<p>De modo geral, os personagens têm a possibilidade de adquirir itens pelos preços indicados nas tabelas e vendê-los pela metade desses valores. Em determinados locais, o mestre do jogo pode decidir que os itens são mais caros, estão disponíveis em quantidades limitadas ou simplesmente não estão disponíveis (consulte o Capítulo 10 para mais informações). Tanto na compra quanto na venda, é possível aprimorar os valores por meio de negociações, sendo a perícia Diplomacia uma ferramenta útil para tal fim.</p>
</section>
<section class="content-section" id="utilizando-transportando">
<h2>Utilizando &amp; Transportando</h2>
<p>O equipamento é inegavelmente útil, mas existe um limite para a quantidade de itens que você pode utilizar ou transportar simultaneamente.</p>
</section>
<section class="content-section" id="restricoes-de-uso">
<h2>Restrições de Uso</h2>
<p>Alguns itens exigem ser empunhados para serem utilizados, tais como armas, escudos, poções, tochas e cajados. Você pode empunhar apenas um item em cada mão, permitindo no máximo dois itens empunhados simultaneamente (considerando que você tenha duas mãos). É possível guardar um item empunhado utilizando uma ação de movimento ou descartá-lo no chão por meio de uma ação livre. Itens que não precisam ser empunhados devem ser vestidos. Você pode usufruir dos benefícios de até quatro itens vestidos ao mesmo tempo, havendo um limite para a quantidade de objetos com os quais você pode lidar simultaneamente. Se você vestir um quinto item, ele não fornecerá benefícios até que outro seja removido. Vestir ou despir um item é uma ação de movimento, e você ainda precisa gastar outra ação de movimento para guardá-lo ou uma ação livre para descartá-lo no chão. Vale ressaltar que esse limite se aplica apenas a itens com benefícios mecânicos; você pode vestir trajes, como um traje de viajante, ou outra vestimenta que não conceda bônus sem afetar esse limite.</p>
</section>
<section class="content-section" id="restricoes-de-carga">
<h2>Restrições de Carga</h2>
<p>A capacidade de transporte de equipamentos é medida em espaços de itens, sendo o padrão de um espaço por item. Contudo, há exceções:</p>
<p class="equipment-bullet">• Itens alquímicos, poções, pergaminhos e outros itens leves ou pequenos ocupam meio espaço. Em outras palavras, dois desses itens ocupam 1 espaço.</p>
<p class="equipment-bullet">• Armas de duas mãos, armaduras leves, escudos pesados, criaturas Minúsculas e outros itens volumosos ou pesados requerem 2 espaços.</p>
<p class="equipment-bullet">• Armaduras pesadas, criaturas Pequenas e outros objetos bastante pesados ou volumosos, como barris ou baús, ocupam 5 espaços.</p>
<p class="equipment-bullet">• Itens extremamente pesados ou volumosos, como uma criatura de porte Médio, ocupam 10 espaços. Objetos ainda maiores podem exigir mais espaços ou tornar-se impossíveis de carregar, a critério do mestre.</p>
<p class="equipment-bullet">• Cada mil moedas, independentemente do tipo, ocupam 1 espaço.</p>
<p>Sua capacidade de transporte é de 10 espaços, com bônus de +2 por ponto de Força (ou -1 por ponto de Força negativo). Se ultrapassar esse limite, ficará sobrecarregado, sofrendo uma penalidade de armadura de -5 e uma redução de 3 metros em seu deslocamento. Não é permitido carregar mais do que o dobro do seu limite. Portanto, um personagem com Força 3 pode transportar até 16 espaços sem penalidade, mas ao exceder esse número, ficará sobrecarregado com até 32 espaços. Não é possível carregar mais do que 32 espaços de itens.</p>
<p>A regra pressupõe que você possui uma mochila para transportar seu equipamento, sendo que a mochila em si não ocupa espaço. Da mesma forma, recipientes cuja única função seja a de transportar outros itens não ocupam espaço. Por exemplo, a bainha de uma espada está contabilizada no espaço ocupado pela própria espada. No entanto, recipientes que proporcionam benefícios próprios, como uma bandoleira de poções, ocupam espaço.</p>
<p>Por fim, fica a critério do mestre decidir se pequenos itens que não têm impacto no jogo - ou seja, são meramente "cosméticos" - precisam ser considerados. É permitido carregar um traje de viajante (ou outra roupa que não ofereça benefícios) sem que isso ocupe espaço .</p>
</section>
<section class="content-section" id="classificacao-de-armas">
<h2>Classificação de Armas</h2>
<p>As armas são categorizadas de acordo com a proficiência necessária para utilizá-las (simples, marciais, exóticas ou de fogo), seu propósito (ataque corpo a corpo ou à distância) e a forma de empunhadura (leve, de uma mão ou de duas mãos).</p>
</section>
<section class="content-section" id="proficiencia">
<h2>Proficiência</h2>
<p><strong>Armas Simples.</strong> Incluem armas de fácil manuseio, como adagas, clavas e lanças. Todos os personagens têm habilidade no uso de armas simples.</p>
<p><strong>Armas Marciais.</strong> Englobam espadas, machados e outras armas destinadas especificamente a combatentes. Boêmios, caçadores, fanfarrões, inquisidores, guerreiros, e rústicos possuem proficiência em armas marciais.</p>
<p><strong>Armas Exóticas.</strong> São armas mais complexas de dominar, como a corrente de espinhos e a espada bastarda. Requerem treinamento específico.</p>
<p><strong>Armas de Fogo.</strong> As armas de pólvora foram trazidas a Pindorama pelos povos caucazis, todas as raças nativas demandam treinamento específico para usá-las.</p>
<p><strong>Penalidade por Não Proficiência.</strong> Caso você ataque com uma arma para a qual não tem proficiência, sofrerá uma penalidade de -5 nos testes de ataque.</p>
</section>
<section class="content-section" id="proposito">
<h2>Propósito</h2>
<p><strong>Combate Corpo a Corpo.</strong> Essas armas são designadas para ataques a alvos próximos. Ao empregar uma arma de combate corpo a corpo, realize um teste de Luta. No momento do ataque com uma arma corpo a corpo, adicione o valor da sua Força aos resultados de dano.</p>
<p><strong>Ataque à Distância.</strong> Estas armas podem ser utilizadas tanto contra alvos próximos quanto a distância. Para realizar um ataque com uma arma de combate à distância, execute um teste de Pontaria. Essas armas se dividem em duas categorias: de arremesso e de disparo.</p>
<p class="equipment-bullet">• Arremesso. Neste caso, a própria arma é lançada, como no uso de adagas ou azagaias. Sacar uma arma de arremesso consome uma ação de movimento. Ao efetuar um ataque com uma arma de arremesso, some o valor da sua Força aos resultados de dano.</p>
<p class="equipment-bullet">• Disparo. As armas de disparo lançam projéteis, como um arco com flechas. Retirar a munição de uma arma de disparo é uma ação livre. Recarregar uma arma de disparo exige ambas as mãos. Ao realizar um ataque com uma arma de disparo, não adicione nenhum valor de atributo aos resultados de dano.</p>
</section>
<section class="content-section" id="manuseio">
<h2>Manuseio</h2>
<p><strong>Leve.</strong> Este tipo de arma é projetado para ser utilizado com apenas uma mão e obtém benefícios do poder Acuidade com Arma.</p>
<p><strong>Uma Mão.</strong> Armas nesta categoria são destinadas ao uso com uma mão, permitindo que a outra fique livre para atividades adicionais.</p>
<p><strong>Duas Mãos.</strong> Armas dessa natureza requerem o uso de ambas as mãos. Libertar uma das mãos é considerado uma ação livre. Empunhá-la novamente exige uma ação de movimento (ou pode ser realizada de forma livre, caso seja possível desembainhá-la dessa maneira).</p>
</section>
<section class="content-section" id="caracteristicas-das-armas">
<h2>Características das Armas</h2>
<p><strong>Preço.</strong> Engloba acessórios básicos, como bainhas para lâminas e aljavas para flechas.</p>
<p><strong>Dano.</strong> Ao efetuar um ataque bem-sucedido, role o dano indicado (adicionando quaisquer modificadores relevantes). O resultado é subtraído dos pontos de vida do alvo. Os valores de dano na tabela referem-se a armas convencionais para criaturas Pequenas e Médias. Consulte a Tabela 4-2: Dano de Armas para informações sobre armas de tamanho menor ou maior.</p>
<p><strong>Crítico.</strong> Um acerto crítico ocorre ao rolar um resultado natural de 20 no dado (ou seja, o dado mostra um 20). Nesse caso, os dados de dano são multiplicados por 2. Bônus numéricos e dados adicionais (como os provenientes do poder Marca da Presa) não são multiplicados. Por exemplo, um dano de 1d8+3 torna-se 2d8+3 durante um acerto crítico.</p>
<p>Algumas armas possuem uma margem de ameaça maior que 20 ou um multiplicador de dano superior a 2. Geralmente, armas mais precisas (como bestas e espadas) possuem uma margem de ameaça maior, enquanto armas mais penetrantes (como arcos e machados) têm um multiplicador maior.</p>
<p>Efeitos que aumentam a margem de ameaça reduzem o número necessário para obter um acerto crítico. Já efeitos que aumentam o multiplicador de dano são adicionados ao número do multiplicador.</p>
<p class="equipment-bullet">• 19 - A arma possui uma margem de ameaça de 19 ou 20.</p>
<p class="equipment-bullet">• 18 - A arma possui uma margem de ameaça de 18, 19 ou 20.</p>
<p class="equipment-bullet">• x2, x3, x4 - A arma causa dano dobrado, triplicado ou quadruplicado em caso de acerto crítico.</p>
<p class="equipment-bullet">• 18/x2 - A arma possui uma margem de ameaça de 18, 19 ou 20 e causa dano duplicado em caso de acerto crítico.</p>
<p>Alcance. Armas dotadas de alcance são destinadas a ataques à distância, e suas categorias de alcance são designadas como curto (9 metros, ou 6 quadrados em um mapa), médio (30 metros ou 20 quadrados) e longo (90 metros ou 60 quadrados). Ataques realizados dentro do alcance estabelecido não sofrem penalidades. No entanto, ao atacar a uma distância de até o dobro do alcance, um redutor de -5 é aplicado ao teste de ataque. Armas desprovidas de alcance podem ser arremessadas em alcance curto com uma penalidade de -5 no teste de ataque.</p>
<p><strong>Tipo.</strong> As armas geralmente causam dano por corte (C), impacto (I) ou perfuração (P). Certas criaturas apresentam resistência ou imunidade a determinados tipos de dano.</p>
<p><strong>Espaço.</strong> Indica quantos espaços a arma ocupa, fator relevante para a capacidade de carga do personagem.</p>
</section>
<section class="content-section" id="habilidades-de-armas">
<h2>Habilidades de Armas</h2>
<p><strong>Algumas armas apresentam uma ou mais das habilidades a seguir.</strong> Essas habilidades estão destacadas em itálico no texto para facilitar sua identificação.</p>
<p><strong>Adaptável.</strong> Uma arma de uma mão com esta habilidade pode ser empunhada com ambas as mãos para aumentar seu dano em um nível.</p>
<p><strong>Ágil.</strong> Pode ser utilizada com Acuidade com Arma, mesmo que não seja considerada uma arma leve.</p>
<p><strong>Alongada.</strong> Dobra o alcance natural do atacante, embora não permita ataques contra adversários adjacentes.</p>
<p><strong>Arremessável.</strong> Esta arma de combate corpo a corpo pode também ser utilizada como uma arma de arremesso.</p>
<p><strong>Desbalanceada.</strong> Impõe uma penalidade de -2 nos testes de ataque.</p>
<p><strong>Discreta.</strong> Fornece um bônus de +5 nos testes de Ladinagem para ocultá-la.</p>
<p><strong>Dupla.</strong> Pode ser empregada com o Estilo de Duas Armas (e habilidades similares) para realizar ataques adicionais, assemelhando-se ao uso de uma arma de uma mão e uma arma leve. Cada "ponta" conta como uma arma separada para efeitos de melhorias e encantos.</p>
<p><strong>Ligeira.</strong> Permite que você utilize sua Destreza em vez de Força nos testes de ataque.</p>
<p><strong>Versátil.</strong> Concede bônus em uma ou mais manobras (cumulativo com outros bônus de itens), de acordo com as características específicas da arma.</p>

<h2>Preparados</h2>
<p><strong>Itens alquímicos de uso único, que produzem efeitos diversos quando ativados.</strong> Normalmente, ingerir um preparado custa uma ação padrão, a menos que na descrição esteja descrito diferente. Alguns preparados possuem a característica Ressaca, isso indica que você sofrerá penalidade de -1 em todos os testes no período que vai desde o fim do efeito do preparado até o próximo descanso. Para criar um preparado, é preciso ter a perícia Ofício (alquimia) e superar uma CD de 15.</p>
<p><strong>Água de Janeiro.</strong> Um frasco pequeno que contém um líquido que revitaliza a mentes cansadas. Ao beber a Água de Janeiro, você gasta uma ação padrão e elimina a condição Frustrado. Se estiver com a condição Esmorecido, role 1d20. Um resultado a cima de 10 você se livra da condição. Se o resultado for menor que 10 você fica Frustrado.</p>
<p><strong>Ácido.</strong> Um frasco de vidro que guarda um líquido corrosivo, capaz de queimar carne e metal. Ao usar o ácido, você gasta uma ação padrão e mira em uma criatura em alcance curto. A vítima sofre 2d4 pontos de dano de ácido, mas pode reduzir o dano à metade com um teste de Reflexos (CD Des).</p>
<p><strong>Antídoto.</strong> Um frasco pequeno que contém um líquido neutralizador de venenos e toxinas. Ao beber o antídoto, você gasta uma ação padrão e elimina qualquer condição envenenado que esteja sofrendo.</p>
<p><strong>Bálsamo Restaurador.</strong> Uma pasta verde e malcheirosa, composta de ervas curativas. Ao aplicar o bálsamo em uma ferida, você gasta uma ação completa e recupera 2d4 pontos de vida.</p>
<p><strong>Bomba.</strong> Uma esfera de metal recheada de pólvora e estilhaços. Para usar a bomba, você deve segurá-la, gastar uma ação de movimento para acender a mecha e uma ação padrão para lançá-la em um ponto em alcance curto. A explosão atinge todas as criaturas em um raio de 3m, causando 6d6 pontos de dano de impacto. Um teste de Reflexos (CD Des) permite escapar da detonação, reduzindo o dano à metade.</p>
<p>Busca-pé. Um bambu com uma mistura de pólvora negra e limalha de ferro seladas com barro em seu interior. Para usar o busca-pé você deve empunha-lo e gastar uma ação de movimento para acende-lo. Uma vez aceso ele tem a aparência de uma espada em chamas. Você pode atacar uma criatura em alcance curto causando 1d8+2 de dano de fogo e deixando o alvo em chamas. Um teste de reflexo reduz o dano a metade e evita a condição. O busca-pé fica aceso até o fim da cena e depois se torna inútil. Você também pode arremessar o busca-pé em alcance médio com os mesmos efeitos de usá-lo em ataques a curta distância.</p>
<p><strong>Cogumelos Mágicos.</strong> Cogumelos que, ao serem comidos, concedem bônus de +2 em testes todos os testes de resistência e +5 PM temporários até o final da cena. Relatos pitorescos dizem que quem devora esses cogumelos, consegue olhar pra dentro de si mesmo, testemunhando a visão da própria alma. Ressaca.</p>
<p><strong>Cosmético.</strong> Um perfume ou uma maquiagem que realça a beleza e o charme. Ao usar um cosmético, você gasta uma ação completa e ganha +2 em testes de perícias baseadas em Carisma até o fim da cena.</p>
<p><strong>Essência de Axumewá.</strong> Uma poção mágica que desperta o amor no coração de quem a bebe. O ser inteligente que ingerir este líquido se apaixonará perdidamente pela primeira criatura que vir (condição enfeitiçado; teste de Vontade CD Car para resistir). O efeito dura 1d3 dias, ou até que o encanto seja quebrado.</p>
<p><strong>Essência de Mana.</strong> Uma poção feita de ingredientes raros e mágicos, que restaura a energia pessoal. Ao beber a essência de mana, você gasta uma ação padrão e recupera 1d4 pontos de mana.</p>
<p><strong>Fogo Alquímico.</strong> Um frasco de cerâmica que contém uma substância inflamável, que explode em contato com o ar. Ao usar o fogo alquímico, você gasta uma ação padrão e mira em uma criatura em alcance curto. A vítima sofre 1d6 pontos de dano de fogo e fica em chamas, sofrendo 1 ponto de dano de fogo por rodada até apagar o fogo. Um teste de Reflexos (CD Des) reduz o dano inicial à metade e evita as chamas.</p>
<p><strong>Fumo de Mascar.</strong> Fumo que produz uma poderosa sensação de queimadura ao ser mascado e suga todas as fontes externas de dor, dando uma falsa sensação invencibilidade. Concede +1 em RD por dado de dano aumenta +1 em dano com armas por dado de dano. Ressaca.</p>
<p><strong>Gingo Biloba.</strong> Extrato de uma planta medicinal que melhora a concentração e a memória. Você se torna imune a condição Esmorecido e recebe +1 em testes envolvendo inteligência ou sabedoria até o próximo descanso. Se você for da classe hermético, aumenta em +1 as magias decoradas no dia.</p>
<p><strong>Goma Oiára.</strong> Pó feito das esponjas marítimas que crescem ao longo da costa de Pindorama. Tem um efeito relaxante e tranquilizador, muito usado pelos navegantes para aliviar o cansaço de longas jornadas. As esponjas são esmagadas e inaladas concedendo +2 em sabedoria até o fim da cena. Ressaca.</p>
<p><strong>Incenso.</strong> Um incenso que facilita a meditação e a conexão com o plano mágico. Ao queimar o incenso e meditar por uma hora, você ganha 2 pontos de mana temporários, que duram até o fim da cena ou até serem gastos.</p>
<p><strong>Kambô.</strong> Secreção extraída da pele do kampü, uma espécie rara de anfíbios. Queima ao entrar em contato com a pele você perde 1 PV, recebe invulnerabilidade a venenos e +2 em testes de fortitude até o final da aventura. O PV perdido só pode ser recuperado ao fim do efeito do Kambô.</p>
<p><strong>Pó do Desaparecimento.</strong> Um pó alquímico que confere invisibilidade temporária a quem o usa. Ao cobrir uma criatura ou objeto com este pó, você gasta uma ação padrão e faz com que ela se torne invisível (como na magia Invisibilidade) por 2d6 rodadas. O efeito é imprevisível, e você não sabe quando a invisibilidade vai acabar.</p>

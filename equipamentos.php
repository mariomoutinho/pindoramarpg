<section class="content-section" id="introducao">
<h2>Introdução</h2>


<p>cenário comercial de uma metrópole de grandes proporções se apresenta como um vibrante caldeirão, repleto de uma diversidade notável de compradores e vendedores. Entre os participantes desse movimentado mercado, encontram-se habilidosos ferreiros zambi e artífices dos povos caucazis, candangos agricultores e talentosos joalheiros angáris. Aventureiros de variadas estaturas, formas e cores também integram esse mosaico, provindos de diversas nações e culturas.</p>
<p>Nos corações pulsantes dos centros urbanos mais grandiosos, como os imensos mercados de ervas do Reino do Grão-Pará, as refinadas joalherias de pedras mágicas em Mi’nérius e a agitada cidade de Brumas, a diversidade de produtos disponíveis para venda é verdadeiramente vasta. Essa ampla oferta abarca desde especiarias exóticas e vestimentas luxuosas até itens tão simples quanto cestos de vime e espadas de treino. Para um aventureiro, a relevância desse mercado vai além da simples transação comercial, uma vez que a aquisição do equipamento adequado pode representar a diferença crucial entre a vida e a morte ao explorar cavernas subterrâneas ou adentrar as selvagens florestas do Grande Verde.</p>
<p>Este capítulo oferece informações minuciosas do comércio de mercadorias tanto mundanas quanto exóticas, detalhando itens que frequentemente se revelam importantes para a sobrevivência de aventureiros diante das ameaças que permeiam os vastos mundos de Pindorama.</p>
</section>
<section class="content-section" id="equipamento-inicial">
<h2>Equipamento Inicial</h2>
<p>Os personagens de 1º nível iniciam sua jornada com os itens providos por sua origem (artífices e cangaceiros também recebem itens iniciais extras), além dos seguintes itens essenciais:</p>
<p class="equipment-bullet">• Uma mochila, um saco de dormir e um traje de viajante.</p>
<p class="equipment-bullet">• Uma arma simples à sua escolha. Se possuir proficiência em armas marciais, também inicia com uma arma marcial à sua escolha.</p>
<p class="equipment-bullet">• Uma armadura de couro, couro batido ou gibão de peles, à sua escolha. Caso tenha proficiência em armaduras pesadas, pode optar por começar com uma brunea. Se possuir proficiência em escudos, inicia também com um escudo leve. Exceção: herméticos e feiticeiros começam sem armadura.</p>
<p class="equipment-bullet">• M$ 4d6, os quais podem ser usados para adquirir itens ou reservados para uso ao longo da aventura.</p>
</section>

<p><strong>Reluzente.</strong> Você pode gastar uma ação de movimento e 2 PM para emitir um brilho de luz. Todos os inimigos em alcance curto devem passar num teste de Reflexos (CD Car) ou ficarão cegos por uma rodada. Um item reluzente é polido e brilhante.</p>
<p><strong>Sombrio.</strong> Você recebe +5 em Furtividade e ignora a penalidade de armadura do item para testes dessa perícia. Um item sombrio é escuro, fosco e bem lubrificado, para não fazer barulho.</p>
<p><strong>Zeloso.</strong> Uma vez por rodada, se um aliado adjacente for alvo de um ataque, você pode gastar 1 PM para se tornar o alvo do ataque, que então é resolvido normalmente.</p>

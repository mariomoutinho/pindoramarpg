<p><strong>Florete Fugaz.</strong> Este florete formidável tem o cabo e a guarda adornados com prata e pedras preciosas. Quando usa a ação agredir, você pode gastar 1 PM. Se fizer isso e acertar um crítico no turno, pode fazer um ataque extra contra a mesma criatura.</p>

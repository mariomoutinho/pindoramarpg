<p>Artefatos</p>
<p><strong>Um artefato representa um objeto mágico singular e extraordinariamente poderoso.</strong> Tipicamente, somente as mais divindades mais influentes possuem o poder de criar tais peças, as quais são sempre concebidas com um propósito específico. Os deuses zelam para que esses artefatos recaiam nas mãos de seus servos mais eminentes, embora, por vezes, o destino conduza essas preciosidades até a posse de simples aventureiros ou vilões.</p>
<p>Diferentemente de simples "itens mágicos", os artefatos são considerados relíquias fabulosas e lendárias. A busca pela recuperação ou destruição de um artefato pode facilmente constituir a espinha dorsal de uma campanha completa. Não existem tabelas para a geração aleatória de artefatos, nem custos predefinidos para sua fabricação. A incorporação desses itens em uma campanha deve ocorrer apenas por escolha deliberada do mestre.</p>
<p>Máscara do Horror</p>
<p>Para corrigir as injustiças no mundo dos mortais, o temível Deus Anhangá incorporava-se no corpo de um veado, transformando-o em seu avatar. Sua pelagem tornava-se alva, e de seus olhos emanavam chamas rubras. Toda criatura que vislumbrasse seu avatar perecia instantaneamente, enquanto a essência de Anhangá o conduzia aos seus domínios no submundo.</p>
<p>A incontáveis eras, Anhangá deparou-se com uma criatura de uma raça mítica que há muito povoou as terras da Pindorama, Micê, uma humana. Diferentemente de todas as outras criaturas, Micê não sucumbiu ao encontro com Anhangá, tampouco demonstrou temor; ao contrário, ela se apaixonou por ele, e o sentimento foi correspondido.</p>
<p>Anhangá conferiu parte de seus poderes a Micê, sacrificando seu avatar para elevá-la à condição de deusa. A essência de Anhangá abandonou o corpo do avatar, que tombou sem vida, e infiltrou-se no ser de Micê, carregando-a para junto de si no submundo. Ao longo dos tempos restaram apenas os ossos do que outrora fora o avatar, imbuídos magicamente da essência do medo.</p>
<p>A fim de regressar à terra dos mortais, Anhangá tomou a decisão de conduzir seu sumo sacerdote, Mañoabá, por meio de visões e sonhos enigmáticos até o local onde os ossos descansavam. Confidenciou-lhe a missão de forjar um elmo com o crânio do antigo avatar, um artefato que conferiria ao Deus o domínio sobre o corpo do usuário e manifestação de seu poder divino.</p>
<p>O artefato de Anhangá é um elmo com aparência de um crânio de um veado com galhadas enormes. Você gasta uma ação padrão para ativa-lo em seguida qualquer criatura que que consiga ver o elmo fica inconsciente até o fim da cena. As vítimas podem fazer um teste de Vontade CD 35, para não perderem a consciência, mas ficam apavoradas. Vítimas imunes a medo ficam abaladas. Depois de usar essa habilidade, você permanece atordoado por 1d4 rodadas (mesmo que seja imune a essa condição). Devotos de Anhangá não sofrem essa penalidade.</p>
<p>Ofá</p>
<p>Odéssi obstinado a criar seu próprio instrumento da caça, convocou a colaboração de seus irmãos para confeccionar tal artefato. Ossain generosamente doou uma mecha de seu cabeleira, um galho da árvore da vida, enquanto Gumedé concedeu seu formão para o entalhe perfeito. Axumewá com sua benevolência única agraciou-o com seus búzios, adornando o arco e dotando-o da habilidade de vislumbrar o futuro, assegurando que o arco jamais erraria o alvo. Kiantomerê banhou a madeira nas águas no seu rio de arco-íris, permitindo que as flechas se metamorfoseassem para atingir destinos diversos, enquanto Ex’us, astuto como sempre, abriu os caminhos para garantir que, não importando a distância, a flecha sempre alcançasse seu destino. Diante desse mosaico divino, Odéssi esculpiu a mira certeira do Ofá.</p>
<p><strong>O ofá é um arco longo, mas parece apenas o arco em madeira escura sem a corda e as flechas.</strong> Porém, quando você o segura e faz o gesto de puxar a corda invisível, o arco cria uma corda e uma flecha de energia dourada. Os ataques feitos com o arco são considerados como conjurações mágicas e jamais podem ser evitados (exceto se o alvo estiver em um ambiente totalmente fechado). O arco do poder é capaz de ler seus desejos, criando diferentes tipos de flechas energéticas a seu comando.</p>
<p class="equipment-bullet">• Flecha Explosiva. 6d6 pontos de dano de fogo no alvo e em todas as criaturas próximas a ele. Essas podem fazer um teste de Reflexos (CD Des) para reduzir o dano à metade.</p>
<p class="equipment-bullet">• Flecha Normal. 6d8 pontos de dano de essência.</p>
<p class="equipment-bullet">• Flecha Piedosa. 7d8 pontos de dano de essência não letal.</p>
<p class="equipment-bullet">• Flecha-Aterrorizante. Não causa dando, mas o oponente deve fazer um teste de Vontade (CD35), em caso de falha fica apavorado e pode refazer o teste a cada começo do seu turno.</p>
<p class="equipment-bullet">• Flecha-Congelante. 6d6 pontos de dano de frio no alvo e ele fica lento (Fortitude CD Des anula o efeito). Se estiver lento e falha no teste, fica paralisado.</p>
<p class="equipment-bullet">• Flecha-Rede. Não causa dano, mas prende a vítima em uma rede de energia. A criatura pode se libertar com um teste de Força ou Acrobacia (CD 35). A rede se desfaz quando a criatura se liberta ou no fim da cena.</p>
<p>Ogó</p>
<p>Um bastão em formato fálico, tradicionalmente feito de madeira que concede ao usuário a capacidade de transmutar entre as dimensões. Você pode lançar Salto Dimensional e Teletransporte, sem gastos de PM, mas apenas em você mesmo. Devoto de Ex’us podem usar essas magias sem restrição.</p>
<p>Opaxorô</p>
<p><strong>Esse cajado, mostra a extensão do poder de Guianalá.</strong> O Opaxorô possui diferentes platôs simbolizados por discos presos em uma única coluna (o cabo do cajado). Cada platô simboliza uma dimensão da existência. Os planos divinos, o plano dos orixás e o plano dos mortais e as dimensões dos ancestrais. Seria então o Opaxorô aquele que sustenta e conecta todos os mundos. De posse desse artefato você pode conjurar as magias Contato Extra Planar, Viagem Planar e Buraco Negro sem custo em PM.</p>
<p>Sacro Taré</p>
<p><strong>Um flauta de bambu que Anhum tocava para anunciar a chegada dos Deuses.</strong> Uma vez por dia se for treinado em Atuação, pode realizar um teste (CD 35) para lançar a magia Intervenção Divina sem gastar PM. Devotos de Anhum recebem redução -10 na CD do teste.</p>
<p>Tridente dos Caminhos</p>
<p>Esse tridente formidável simboliza a união do poder mágico dos quatro elementos água, ar e fogo em suas pontas superiores e terra na ponta inferior. Como sendo o mundo a união harmônica desses elementos ele simboliza que Ex’us está no centro de tudo, em todos os lugares. Você pode gastar e uma ação padrão para realizar a magia Barragem Elemental sem custo em PM. Se for um devoto de Ex’us além da magia você pode usar os aprimoramentos dela sem custo em PM.</p>
<p>Edã</p>
<p>Um bordão adornado por duas serpentes entrelaçadas e uma flecha em sua ponta. Esse artefato é uma arma de poder inimaginável criada por Kiantomerê, Deus das transformações, que como a própria divindade, desafia as leis da existência. Quem o empunha pode lançar qualquer magia de transmutação que tenha conhecimento ou que já tenha testemunhado — seja de origem arcana ou divina — como se fosse um dom natural, sem consumir sua energia vital. O portador do cajado também pode potencializar seus encantamentos, mas isso tem um preço. Para usar o cajado, é preciso vencer um desafio de Misticismo (CD 20 + o custo em PM do feitiço, incluindo os aprimoramentos). Se o desafio for fracassado, o feitiço não surte efeito ou causa uma consequência imprevista (mudança de alvo, inversão de efeito, teletransporte para um lugar aleatório...). Ou pelo menos é o que parece. Na verdade, toda vez que um feitiço "falha", isso significa que Kiantomerê, o deus das transformações, está no comando — e as coisas estão acontecendo exatamente como ele planejou, seguindo uma trama complexa e oculta ao dono do cajado.</p>
<p>Ibirí</p>
<p>O Ibiri vai além da mera condição de um artefato, ele é, de fato, como um filho para grande yabá (orixá feminina) Namburuk. Conforme as lendas narram, este artefato não foi construído ou mesmo presenteado, mas sim embalado nos braços da mais velha das Divindades desde o momento de sua criação. Assim como a divindade, a qual está vinculado, o cetro carrega consigo o domínio sobre as transições entre a vida e da morte. Ao caminhar das eras Namburuk entendeu que o Ibirí precisava trilhar seu próprio caminho e o deixou se libertar de seus braços e seguir para o mundo dos mortais possibilitando que seus servos mais devotados encontrassem os corpos caídos e os acolhessem de volta, conduzindo as almas através das passagens entre os planos da existência.</p>
<p>O Cetro é feito de várias palhas de diferentes palmeiras amarradas com faixas e contas na cor roxas. Como a divindade, rege os domínios do princípio e termino da vida. Quem o empunha pode lançar qualquer magia de necromancia que tenha conhecimento ou que já tenha testemunhado — seja de origem arcana ou divina — como se fosse um dom natural, sem consumir sua energia vital. O portador do cajado também pode potencializar seus encantamentos, mas isso tem um preço. Para usar o cajado, é preciso vencer um desafio de Misticismo (CD 20 + o custo em PM do feitiço, incluindo os aprimoramentos). Se o desafio for fracassado, o feitiço não surte efeito, ou seu custo em PM total (incluindo os aprimoramentos) é debitado em PV. Como senhora da vida e da morte, Namburuk pedirá de volta o que é dela.</p>

<h2>Escudos Específicos</h2>
<p><strong>Escudo de Kuaracyr.</strong> Este escudo pesado guardião é forjado na forma de um sol estilizado. Você pode gastar uma ação padrão e 10 PM para fazê-lo emitir uma luz brilhante e quente num cone com alcance curto. A luz gera os efeitos da magia Visão da Verdade e causa 6d6 pontos de dano de fogo em todos os seus inimigos (mortos-vivos e criaturas vulneráveis a luz solar sofrem 6d8 pontos de dano). Você pode gastar 1 PM no início de cada um de seus turnos para manter a luz.</p>
<p><strong>Escudo do Conjurador.</strong> Este escudo leve defensor tem uma pequena tira de couro na parte interna, sobre a qual um conjurador pode lançar uma magia. A magia não surte efeito na hora; em vez disso, fica inscrita na tira. A tira pode então ser lida como um pergaminho. Uma vez que a magia seja lançada, outra pode ser inscrita.</p>
<p><strong>Escudo do Eclipse.</strong> Este escudo pesado defensor é completamente negro e parece absorver a luz. Ele fornece redução de trevas 10 e causa +1d8 de dano de trevas num ataque. Além disso, você pode gastar uma ação de movimento e 2 PM para lançar Escuridão.</p>
<p><strong>Escudo Espinhoso.</strong> Este escudo pesado defensor é coberto de espinhos. Você pode gastar uma ação de movimento e 2 PM para disparar um espinho em um alvo em alcance curto. O espinho acerta automaticamente e causa 1d10+2 pontos de dano de perfuração.</p>
<p><strong>Escudo da Onça.</strong> Este escudo pesado defensor é forjado como uma cabeça de Onça-pintada rugindo. Uma vez por rodada, você pode gastar 2 PM para fazer a cabeça ganhar vida e morder uma criatura adjacente. A mordida acerta automaticamente e causa 2d6+2 pontos de dano de perfuração.</p>

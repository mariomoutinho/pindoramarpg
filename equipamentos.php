<h2>Alimentação</h2>
<p><strong>Aventurar-se é viajar sem rumo, em busca de emoção e glória.</strong> Mas nem só de lutas e perigos vive um aventureiro. Às vezes, é preciso parar e comer algo. E é aí que as tavernas e estalagens entram, oferecendo comida, bebida e descanso aos viajantes. Cada uma tem sua história, seu segredo, seu clima. Quem sabe o que se pode encontrar em uma dessas paradas? Só há um jeito de saber: entrando e pedindo um prato.</p>
<p><strong>Acarajé.</strong> Prato típicos dos povos zambi, trata-se de bolinhos fritos de massa de feijão fradinho, recheado com vatapá, camarão seco, caruru e salada. É um prato típico dos Zambi. São bem gordurosos e pouco nutritivos, no entanto, são deliciosas e capazes de animar qualquer um. Receba +1d6 em um teste à sua escolha realizado até o fim do dia. Para não esquecer, deixe 1d6 em cima da sua ficha.</p>
<p><strong>Baião de Dois.</strong> Um prato que nasceu da necessidade e da criatividade dos povos de Cearinas, um reino formado pelos nômades do Deserto do Sertão, onde a fome e a seca eram constantes. Eles aproveitavam o que tinham à mão, arroz e feijão, e misturavam com carne seca e queijo de coalho, criando assim essa delícia. Com o tempo, o baião de dois ganhou novos sabores e ingredientes, e se espalhou por outras terras e reinos. Ganhe +5 PV temporários.</p>
<p><strong>Café de Soldado.</strong> Uma bebida forte e quente, feita com café e outros ingredientes secretos, que desperta os sentidos e acelera os reflexos. Ao bebe-la, você gasta uma ação de movimento e ganha + 1d6 nos testes de Iniciativa.</p>
<p><strong>Caipirinha.</strong> Uma bebida alcoólica feita de cachaça, limão, açúcar e gelo. Conhece RD +1 por dado de dano e -1 em destreza até final da cena. Ressaca.</p>
<p><strong>Caldeirada.</strong> Um prato de peixe e frutos do mar, cozidos em um caldo de legumes. Um prato comum nas regiões litorâneas de Pindorama que garante de um descanso relaxante. Na sua próxima noite de sono, aumente a recuperação de PM em +1 por nível.</p>
<p><strong>Cannabis.</strong> Servido como chá ou quente ou fumando essa erva você ativa conexões no cérebro, proporcionando +2 PM temporários.</p>
<p><strong>Cerveja.</strong> Uma caneca espumante de cevada fermentada. Bebida clara, refrescante e agradavelmente amarga. Ao beber você ganha um bônus de +2 em força e -1 em destreza até o final da cena. Ressaca.</p>
<p><strong>Chimarrão.</strong> Chá popular do reino dos Pampas, feito das folhas da erva-mate desidratada e moída. Confere imunidade a doenças por 1d6 dias e aumenta a recuperação de PV por descanso em +1. 80</p>
<p><strong>Cuscuz Recheado.</strong> Banquete feito com farinha de milho flocada e cozida no vapor com legumes carnes e ovos. Aumenta +1 em força e +1 em constituição (+5 PV temporário). Além disso recebem +1 por dado em cura recebida. Esses efeitos duram 1 dia.</p>
<p><strong>Pato ao Tucupi.</strong> É elaborado com carne de pato selvagem banhada com tucupi - líquido de cor amarela extraído da raiz da mandioca brava - temperado com jambu. Concede +1 em força, +1 em sabedoria e +1 em bônus recebidos por magia.</p>
<p><strong>Prato do Aventureiro.</strong> Um cozido de galinha com legumes, esta é uma refeição simples, porém nutritiva. Na sua próxima noite de sono, aumente a recuperação de pontos de vida em +1 por nível.</p>
<p><strong>Ração de Viagem.</strong> Ideal para jornadas, uma porção desta ração alimenta uma pessoa por um dia. Feita de alimentos conservados, como carne defumada, frutas secas, pão, queijo e biscoitos. Mantida seca, dura bastante, mas quando molhada, estraga em 24 horas.</p>
<p><strong>Refeição Comum.</strong> Uma refeição típica inclui pão, queijo, cozido de carne ou galinha com legumes e uma caneca de bebida, geralmente cidra, vinho ou cerveja.</p>

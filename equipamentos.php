<h2>Venenos</h2>
<p>Substâncias letais, extraídas da natureza ou criadas por alquimia, que podem matar ou debilitar seres vivos. A menos que seja dito o contrário, a dificuldade para produzir qualquer veneno é 20.</p>
<p><strong>Beladona.</strong> Uma planta extremamente tóxica que afeta o sistema nervoso da vítima. Na ingestão, a vítima fica paralisada (lenta) por 3 rodadas. A CD para fabricar e resistir a este veneno aumenta em +5.</p>
<p><strong>Boa Noite.</strong> Um gás sonífero. Inalação, a vítima fica inconsciente (enjoada por 1 rodada).</p>
<p><strong>Cicuta.</strong> Uma planta cuja ingestão pode causar náusea, dores e até a morte. Na ingestão, a vítima perde 1d12 PV por rodada durante 3 rodadas (perde 1d12 PV).</p>
<p><strong>Comigo Ninguem Pode.</strong> Um veneno extraído da folha de uma planta. Contato, o alvo sofre 1d6 de dano ácido por 1d4 rodadas e fica enjoado. (perde 1d6 PV).</p>
<p><strong>Essência de Sombra.</strong> Produzida a partir de compostos alquímicos que canalizam energia das trevas. Contato, a vítima fica debilitada (fraca).</p>
<p><strong>Lança Perfume.</strong> Um gás que faz a vítima rir descontroladamente e agir de forma caótica. Na inalação, a vítima fica confusa (lenta por 1 rodada).</p>
<p><strong>Névoa Tóxica.</strong> Um gás verde que queima e corrói a pele e os pulmões. Na inalação, a vítima perde 1d12 PV por rodada durante 3 rodadas (perde 1d12 PV).</p>
<p><strong>Passiflora.</strong> Uma planta que possui efeitos sedativos e relaxantes. Na dose adequada, a passiflora se torna um poderoso sonífero. Ingestão, vítima fica inconsciente.</p>
<p><strong>Peçonha Comum.</strong> Um veneno típico, extraído de animais ou plantas tóxicas. No contato, a vítima perde 1d12 PV.</p>
<p><strong>Peçonha Concentrada.</strong> Uma dose concentrada da peçonha comum. No contato, a vítima perde 1d12 PV por rodada durante 3 rodadas (perde 1d12 PV).</p>
<p><strong>Peçonha Potente.</strong> Um veneno poderoso, extraído de animais ou plantas perigosos. No contato, a vítima perde 2d12 PV por rodada durante 3 rodadas (perde 2d12 PV).</p>
<p><strong>Pó de Corpo-seco.</strong> Um veneno letal, usado para assassinar alvos poderosos. Na ingestão, a vítima perde 4d12 PV por rodada durante 5 rodadas (perde 4d12 PV). A CD para fabricar e resistir a este veneno aumenta em +5.</p>

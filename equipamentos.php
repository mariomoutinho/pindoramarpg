<section class="content-section" id="classificacao-de-armas-2">
<h2>Classificação de Armas</h2>
<p>As armaduras são divididas em categorias leves e pesadas, levando em consideração sua praticidade de uso e mobilidade.</p>
</section>
<section class="content-section" id="proficiencia-2">
<h2>Proficiência</h2>
<p><strong>Armaduras Leves.</strong> Confeccionadas a partir de tecido, couro ou peles, estas armaduras oferecem proteção limitada, porém garantem uma ampla liberdade de movimentos. Vestir ou remover uma armadura leve exige uma ação completa.</p>
<p><strong>Armaduras Pesadas.</strong> Compostas por cota de malha (trama com anéis metálicos) ou placas de aço, as armaduras pesadas proporcionam uma proteção substancial, porém restringem a agilidade do usuário. Ao utilizar uma armadura pesada, a Destreza não é aplicada à Defesa, e o deslocamento é reduzido em 3 metros. O processo de vestir ou remover uma armadura pesada leva cinco minutos. Dormir usando esse tipo de armadura resulta em fadiga ao longo do dia.</p>
<p><strong>Escudos.</strong> Existem opções de escudos leves e pesados, e um personagem proficiente nessas armas é capaz de utilizar ambos. Colocar ou retirar qualquer tipo de escudo é considerado uma ação de movimento.</p>
<p><strong>Ataque com Escudo.</strong> Caso tenha proficiência em armas marciais, é possível utilizar um escudo para atacar, mas isso implica na perda do bônus na Defesa até o próximo turno. Escudos leves causam 1d4 pontos de dano de impacto, enquanto escudos pesados causam 1d6 pontos de dano de impacto, ambos com multiplicador crítico x2. É importante ressaltar que, apesar de sua utilização em ataques, escudos não são considerados armas.</p>
<p><strong>Penalidade por Falta de Proficiência.</strong> Um personagem que utiliza uma armadura ou escudo sem a devida proficiência sofre a penalidade da armadura em todas as perícias baseadas em Força e Destreza.</p>
</section>
<section class="content-section" id="caracteristicas-das-armaduras-e-escudos">
<h2>Características das Armaduras e Escudos</h2>
<p><strong>Preço.</strong> Este representa o custo total de armaduras completas. Geralmente, as "partes" das armaduras não são comercializadas separadamente e não conferem proteção quando utilizadas isoladamente.</p>
<p><strong>Bônus na Defesa.</strong> Cada tipo de armadura proporciona um bônus na Defesa do usuário, sendo que armaduras mais pesadas oferecem um bônus maior. Não é possível vestir uma armadura sobre outra, mas é permitido o uso simultâneo de uma armadura e um escudo (com acúmulo dos bônus). No entanto, não é permitido empunhar dois escudos ao mesmo tempo.</p>
<p><strong>Penalidade de Armadura.</strong> Devido ao seu peso e rigidez, a maioria das armaduras representa um obstáculo para a realização de perícias que exigem agilidade. Aplique a penalidade de armadura em testes de Acrobacia, Furtividade e Ladinagem (bem como em testes de Atletismo para natação). Vale ressaltar que penalidades de armaduras e escudos se acumulam.</p>
<p><strong>Espaço.</strong> Determina quantos espaços a armadura ou escudo ocupa, sendo um fator crucial para a capacidade de carga do personagem.</p>
</section>
<section class="content-section" id="descricao-das-armaduras-e-escudos">
<h2>Descrição das Armaduras e Escudos</h2>
<p><strong>Armadura Acolchoada.</strong> Uma túnica almofadada confeccionada em linho ou lã. Representa a armadura mais leve, proporcionando proteção para todo o corpo e conferindo um bônus de +2 em Fortitude.</p>
<p><strong>Armadura Completa.</strong> A mais robusta e pesada das armaduras, composta por placas de metal forjadas e ajustadas de forma a cobrir todo o corpo. Inclui uma túnica acolchoada para ser utilizada sob as placas. Correias e fivelas distribuem o peso da armadura pelo corpo inteiro. Deve ser feita sob medida para cada usuário, e um ferreiro cobra T$ 200 para adaptar essa armadura a um novo usuário.</p>
<p><strong>Armadura de Couro.</strong> O peitoral desta armadura é confeccionado com couro curtido em óleo fervente, tornando-o mais rígido, enquanto as demais partes são feitas de couro flexível.</p>
<p><strong>Brunea.</strong> Um colete de couro coberto com plaquetas de metal sobrepostas, assemelhando-se às escamas de um peixe. Devido à sua produção econômica, é a armadura mais comum no Reinado, utilizada por soldados de infantaria e guardas de castelo.</p>
<p><strong>Cota de Anéis.</strong> Composta por uma série de anéis metálicos costurados em uma base de couro, esta armadura resulta em uma redução de 1,5 metros em seu deslocamento.</p>
<p><strong>Cota de Malha.</strong> Uma longa veste de anéis metálicos interligados, formando uma malha flexível e resistente que se estende até os joelhos.</p>
<p><strong>Cota de Talas.</strong> Composta por tiras horizontais de metal, esta armadura pesada é amplamente utilizada por legionários e inquisidores do Império do Brazil.</p>
<p><strong>Couraça.</strong> A mais robusta das armaduras leves, composta por uma placa metálica que protege o peito e as costas, presa sobre um casaco de couro. Couro Batido. Uma versão mais pesada da armadura de couro, reforçada com rebites de metal.</p>
<p><strong>Escudo Leve.</strong> Geralmente confeccionado em madeira, este escudo é amarrado no antebraço, deixando a mão livre. Permite que o usuário carregue um objeto na mão que empunha o escudo, mas impede o manuseio de uma arma.</p>
<p><strong>Escudo Pesado.</strong> Tipicamente feito de aço, este escudo é preso ao antebraço e requer firmeza para ser empunhado, impedindo o usuário de utilizar aquela mão.</p>
<p><strong>Escudo Torre.</strong> Este imponente escudo de formato grande e retangular é confeccionado com madeira, reforçado com aço e revestido de couro. Dada a sua dimensão, não é adequado para uso ofensivo ou montado. Você tem a opção de utilizar uma ação de movimento para fixar o escudo no chão, transformando-o em uma barreira que proporciona cobertura leve.</p>
<p><strong>Gibão de Peles.</strong> Principalmente utilizado por bárbaros e selvagens, esta armadura é formada por várias camadas de peles e couro de animais.</p>
<p><strong>Meia Armadura.</strong> Uma cota de malha reforçada com placas de metal.</p>

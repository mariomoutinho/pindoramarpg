<p><strong>Garra do Diabo.</strong> Uma erva rara que nasce apenas nas terras amaldiçoadas do Vale do Anhangabaú. As lendas dizem que a erva contém a essência da própria Micê, a rainha do submundo. Ao comer a erva, os dados de dano de magias de energia necrótica aumentam em uma categoria — de d4 para d6, d6 para d8, d8 para d10 e d10 para d12 (o máximo).</p>

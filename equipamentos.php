<h2>Serviços</h2>
<p>Hospedagem</p>
<p>Estalagens e tavernas são locais onde aventureiros repousam ou se preparam para suas próximas jornadas. Estalagens funcionam como hospedarias, oferecendo quartos para aluguel e refeições. Tavernas, por outro lado, são similares a bares, proporcionando refeições, bebidas e, às vezes, espetáculos, frequentemente apresentados por boêmios, além de serem excelentes fontes de informações. Os preços das estadias abaixo são por noite, incluindo uma refeição comum, e determinam a recuperação de PV e PM (consulte a página 106 para mais detalhes).</p>
<p><strong>Comum.</strong> Um espaço no salão principal. Com sorte, o taverneiro manterá a lareira acesa, garantindo uma noite menos fria. Contudo, a companhia incluirá pulgas, ratos e baratas. A refeição consiste em pão, sopa e água. Recupera 1 PV e 1 PM por nível.</p>
<p><strong>Confortável.</strong> Um quarto pequeno, porém, privativo, com uma cama simples e um colchão de palha, além de um baú para armazenar pertences. A refeição inclui pão, queijo, cozido de galinha com legumes e cerveja (não muito forte). Recupera 2 PV e 2 PM por nível.</p>
<p><strong>Luxuosa.</strong> Um quarto espaçoso, com colchão de algodão ou penas, cortinas nas janelas, banheira de água quente e outros luxos. A refeição contempla carne, frutas, doces e uma taça de vinho de qualidade. Dependendo da estalagem, ela também pode servir um prato exclusivo a critério do mestre. Acomodações desse nível estão disponíveis nas melhores estalagens, geralmente encontradas apenas em grandes cidades e metrópoles. Recupera 3 PV e 3 PM por nível.</p>
</section>
<section class="content-section" id="mercenarios">
<h2>Mercenários</h2>
<p><strong>Mercenários são como parceiros (veja a página 213).</strong> O preço indicado é o que eles cobram para lutar ao seu lado por uma cena. Eles o seguem, ocupando seu limite de parceiros, mas sem fazer nada, até que você peça sua ajuda. Então, eles mostram seu valor até o fim da cena. Depois de ajudá-lo, o mercenário parte. Com a permissão do mestre, você pode contratar um mercenário para uma aventura inteira pelo triplo do preço. Cada mercenário tem uma descrição que indica seu tipo e nível de poder.</p>
<p><strong>Alquimista de Batalha.</strong> Um artífice versado em poções alquímicas e seu uso em combate. Destruidor veterano.</p>
<p><strong>Aprendiz de Guilda.</strong> Um assistente qualificado, capaz de auxiliá-lo em sua profissão. Artesão iniciante.</p>
<p><strong>Aprendiz de Mago.</strong> Um erudito das artes mágicas, capaz de apoiá-lo com suas magias (mas ainda incapaz de lançar feitiços por conta própria). Magivocador iniciante.</p>
<p><strong>Arauto.</strong> Um criado treinado para proclamar sua presença com pompa. Emissário veterano.</p>
<p><strong>Arqueiro.</strong> Um guerreiro especializado em armas de projétil, como arcos ou bestas. Atirador iniciante.</p>
<p><strong>Bando de Aldeões.</strong> Uma turba de miseráveis descalços armados com ancinhos, cacetes e outras armas improvisadas. Turba iniciante.</p>
<p><strong>Batedor.</strong> Um explorador sagaz e familiarizado com as terras selvagens. Vigilante veterano.</p>
<p><strong>Bibliotecário Místico.</strong> Um aficionado por magia, cheio de livros e pergaminhos. Místico veterano.</p>
<p><strong>Carregador de Tochas.</strong> Um criado que auxilia com sua tralha (apesar do nome, leva mais do que tochas). Carregador iniciante.</p>
<p><strong>Conselheiro.</strong> Um erudito de vários temas. Sábio veterano.</p>
<p><strong>Guarda-Costas.</strong> Um mercenário especialista em proteger seu cliente e enfrentar seus adversários. Combatente e guardião iniciante.</p>
<p><strong>Herbalista.</strong> Um perito em ervas e remédios naturais. Médico iniciante.</p>
<p><strong>Homem de Armas.</strong> Um guerreiro treinado em armas de combate próximo, como espadas e machados. Fortão iniciante.</p>
<p><strong>Irregulares.</strong> Soldados que não pertencem a nenhum exército ou companhia mercenária oficial, com pouco preparo e equipamento variado. Infantaria veterana.</p>
<p><strong>Matador.</strong> Um assassino profissional, discreto e mortal. Assassino veterano.</p>
<p><strong>Sombra.</strong> Um mercenário habituado a agir furtivamente e revelar segredos ocultos. Espião veterano.</p>
</section>
<section class="content-section" id="outros-servicos">
<h2>Outros Serviços</h2>
<p><strong>Condução.</strong> Inclui viagens terrestres (em carroças), marítimas (em navios) ou aéreas (paus-de-arara ou dirigíveis). Viajar em Pau-de-arara as vezes pode ser arriscado: a cada 100 km, há 1 chance em 20 de queda (não fatal).</p>
<p><strong>Curandeiro.</strong> O preço para receber cuidados prolongados ou tratamento contra doenças ou venenos (consulte Cura em Perícias). Isso pressupõe que você se desloque até a residência do curandeiro ou as santas Casas de misericórdia, pois eles geralmente não acompanham aventureiros em suas jornadas.</p>
<p><strong>Mensageiro.</strong> Inclui a entrega de mensagens a pé, por cavaleiros, navios ou os serviços de entrega por pássaros da Gazeta de Guanabara.</p>
<p><strong>Magias.</strong> Este é o custo para lançar uma magia em uma situação comum, onde o ato não apresenta riscos para o conjurador. Se você solicitar que o conjurador o acompanhe em uma aventura, a resposta padrão será "não, obrigado" e um possível aumento de custo nos serviços. Para um conjurador acompanha-lo em suas aventuras, veja a sessão “Mercenários”.</p>
<p>Itens especiais são versões mais poderosas de certos itens, como armas forjadas em aço damasco e outras técnicas exclusivas dos angáris, ou alquímicos que demandam uma tecnologia robusta e as vezes magia ocultas para serem fabricados.</p>
</section>
<section class="content-section" id="itens-superiores">
<h2>Itens superiores</h2>
<p>Itens superiores são tesouros raros e cobiçados, forjados com as melhores técnicas, matéria prima diferenciada, arte e magia. No jogo, itens superiores têm de uma a quatro melhorias. Cada melhoria traz uma vantagem, mas também um custo. Por exemplo, uma maça comum vale T$ 12. Uma maça superior com uma melhoria vale T$ 312. E uma maça com quatro melhorias vale impressionantes T$ 18.012!</p>
<p>Itens com uma melhoria são caros, mas não impossíveis de encontrar. Por exemplo, um castelo rico e poderoso de Lusília pode armar seus guardas com armas assim. Itens com duas melhorias são preciosidades, e só existem em pequeno número. Um capitão da ordem dos revolucionários da estrela vermelha pode ter um item desses. Mas itens com três ou quatro melhorias são lendários, tão ou mais famosos que seus donos. O próprio rei da coroa lusina ou a princesa herdeira pode ter um item desses, e ele certamente será uma relíquia de família, passada de geração em geração.</p>
</section>
<section class="content-section" id="melhorias">
<h2>Melhorias</h2>
<p>Somente objetos das categorias armas, armaduras e escudos, ferramentas, vestuário e esotéricos são elegíveis para a incorporação de melhorias. As melhorias específicas para cada categoria de objeto estão detalhadas na Tabela 3-8. Cada melhoria pode ser aplicada somente uma vez a um determinado objeto.</p>
<p><strong>Ajustada.</strong> O item foi feito sob medida, com peças precisas e encaixes perfeitos. O item tem sua penalidade de armadura reduzida em 1.</p>
<p><strong>Ampliador.</strong> Quando você lança uma magia que tenha área em metros, você pode usar +1 PM para ampliar a área dessa magia em +3m.</p>
<p><strong>Aprimorado.</strong> O item foi construído com esmero e qualidade, usando os melhores materiais disponíveis. Esta melhoria só pode ser aplicada a uma ferramenta ou vestuário que altere uma perícia (diminuindo uma penalidade ou concedendo um bônus) e fornece um bônus de +1 nessa perícia (ou aumenta o bônus existente em +1). Por exemplo, uma ferramenta de ladrão aprimorada fornece +1 em Ladinagem e uma camisa bufante aprimorada fornece +2 em Atuação.</p>
<p><strong>Armazenador.</strong> Um esotérico armazenador é um receptáculo de mana, capaz de guardar energia mágica para uso posterior. Você pode gastar até 5 PM para transferir a mesma quantidade de pontos de mana para o item. Esses PM podem ser usados para custear magias lançadas com o item, e ficam nele até serem usados (você pode recuperar seus PM com descanso, sem ser afetado por essa reserva extra).</p>
<p><strong>Atroz.</strong> A arma é uma monstruosidade de pontas, ganchos e saliências. É difícil manejá-la sem se ferir, mas ela fornece +2 nas rolagens de dano. Pré-requisito: Cruel.</p>
<p><strong>Banhado a Ouro.</strong> Uma melhoria preferida por nobres vaidosos ou aventureiros recém-enriquecidos. Fornece +2 em Diplomacia. O mestre pode mudar o bônus para uma penalidade de –2 contra pessoas que detestam ostentação. Além disso, pode despertar a cobiça de ladrões.</p>
<p><strong>Canalizador.</strong> O esotérico possui uma gema mística que permite que você canalize mais mana do que o normal. O máximo de PM que você pode gastar em magias aumenta em +1.</p>
<p><strong>Cano Duplo.</strong> (Armas de Fogo). A arma tem a capacidade de portar duas balas simultaneamente. Cada cano exige uma ação separada para recarga.</p>
<p>Cartucho de Papel. Esse aprimoramento é exclusivo para munição em forma de pacotes. Consiste em um pequeno tubo de papel tratado alquimicamente que contém tanto a pólvora quanto a bala. A recarga é significativamente mais rápida com cartuchos de papéis, diminuindo em um passo. Contudo, a probabilidade de falha aumenta em +2 (um 3 no d20 é considerado falha) e carregar qualquer quantidade de invólucros de papel impõe um redutor de -2 em testes de resistência contra efeitos de fogo. Se o portador obtiver um resultado de 1 em um teste de resistência contra efeitos de fogo, ele entra em chamas e todos os seus cartuchos de papel são destruídos. Essa modificação só pode ser aplicada a balas.</p>
<p><strong>Certeira.</strong> Forjada para ser mais precisa e equilibrada, a arma fornece +1 nos testes de ataque.</p>
<p><strong>Cravejado de Gemas.</strong> É fácil impressionar alguém que tem dinheiro suficiente para enfeitar um item com gemas. Fornece +2 em Enganação. Assim como um item banhado a ouro, um item cravejado de gemas pode atrair a cobiça de ladrões.</p>
<p><strong>Cruel.</strong> A arma possui rebarbas, espinhos e até mesmo lâminas extras que a tornam mais cruel. Ela fornece +1 nas rolagens de dano.</p>
<p><strong>Delicada.</strong> A armadura foi feita com os materiais mais leves possíveis. As placas têm a espessura mínima necessária para oferecer a proteção adequada. Esta melhoria só pode ser aplicada a armaduras pesadas e permite que o personagem aplique 1 ponto de sua Destreza na Defesa (ou de outro atributo, caso o utilize em vez de Des). Uma armadura não pode ser delicada e reforçada.</p>
<p><strong>Discreto.</strong> O item é camuflado como outro item inocente (como um florete escondido em uma bengala) ou modificado para ser telescópico (podendo se retrair para ocupar menos espaço). O item ocupa –1 espaço (mínimo 1) e fornece +5 em testes de Ladinagem para ser ocultado.</p>
<p><strong>Energéticos.</strong> O item possui pequenos cristais mágicos que amplificam as energias arcanas. Suas magias que causam dano causam +1d6 pontos de dano do mesmo tipo.</p>
<p><strong>Engraxado.</strong> Uma solução alquímica especial possibilita que a arma seja sacada rapidamente, conferindo um bônus de +2 em iniciativa. Entretanto, o usuário sofre uma penalidade de -2 em testes para resistir à manobra de desarme.</p>
<p><strong>Equilibrada.</strong> A arma possui um peso e uma forma perfeitos, que permitem movimentos ágeis e precisos. Ela fornece +2 em testes de manobras (desarmar, derrubar e etc.).</p>
<p><strong>Espinhosa (Armadura).</strong> A armadura é adornada com espinhos afiados, que podem ferir quem se aproximar demais. Se o usuário agarrar ou for agarrado por uma criatura, causa dano de perfuração igual a sua Força a essa outra criatura. O dano é causado quando a manobra é feita e no início de cada turno do personagem, enquanto ela for mantida.</p>
<p><strong>Espinhoso (Escudo).</strong> O escudo possui pontas ou lâminas que aumentam o dano de um ataque com escudo em um passo.</p>
<p><strong>Extensor.</strong> O item estende suas magias. Quando você lança uma magia que tenha alcance curto ou médio, você pode gastar +2 PM para aumentar o alcance dessa magia em uma categoria (de curto para médio ou de médio para longo).</p>
<p><strong>Harmonizada (Arma).</strong> A arma foi tratada com óleos místicos que a deixaram em sintonia com a aura de seu usuário. Escolha uma habilidade ativada ao se fazer um ataque ou usar a ação agredir e que custe pontos de mana. Esta habilidade tem seu custo em PM reduzido em –1 se utilizada com esta arma. Pré-requisito: outra melhoria qualquer.</p>
<p><strong>Harmonizado (Esotérico).</strong> Escolha uma magia. Seu custo diminui em –1 PM. Você pode mudar a magia afetada pelo item com um ritual que exige um dia e T$ 100 em ingredientes.</p>
<p><strong>Injeção Alquímica.</strong> Um pequeno frasco de cerâmica ou vidro é acoplado à arma, junto com um mecanismo injetor ativado por impacto. Um ataque que acerte causa seu dano normal e libera uma dose de um preparado (como ácido ou fogo alquímico) ou de água benta, que atinge o alvo automaticamente. A melhoria tem espaço para 2 doses. Carregá-la exige uma ação completa e o gasto dos itens com os quais você quiser carregá-la.</p>
<p><strong>Macabro.</strong> O item é pintado com sangue seco, esculpido na forma de uma caveira ou decorado com pedaços de orelhas, dedos e olhos. Essa aparência horripilante fornece +2 em Intimidação, mas impõe uma penalidade de –2 em Diplomacia.</p>
<p><strong>Maciça.</strong> A arma é feita com material denso, que confere mais força aos seus golpes. O multiplicador de crítico da arma aumenta em 1 ponto. Uma arma não pode ser maciça e precisa.</p>
<p><strong>Material Especial.</strong> A arma, armadura, escudo ou item esotérico é feito de, ou revestido com, um material especial. Cada material oferece um benefício, que se soma aos outros benefícios de melhorias, mas também tem um custo extra que deve ser pago além do preço da melhoria. Veja a seção seguinte para a descrição dos materiais especiais. Criar um item de material especial é uma melhoria porque materiais especiais exigem um trabalho mais sofisticado do que materiais comuns.</p>
<p><strong>Mira Telescópica.</strong> Aumenta o alcance da arma em uma categoria (de curto para médio, de médio para longo) e o alcance da habilidade Ataque Furtivo para médio. Esta melhoria só pode ser aplicada em armas de disparo (exceto fundas).</p>
<p><strong>Poderoso.</strong> A CD para resistir às suas magias aumenta em +1.</p>
<p><strong>Polida.</strong> A armadura ou escudo foi feita com metais brilhantes. Além de bela, a luz refletida cega inimigos. Em ambientes iluminados, o bônus de Defesa do item aumenta em +5, mas apenas na primeira rodada de combate — depois disso, os inimigos se adaptam ao reflexo.</p>
<p><strong>Precisa.</strong> Um cuidado especial foi tomado ao forjar o aço desta arma, para que sua lâmina se mantenha sempre afiada. A margem de ameaça aumenta em 1 ponto. Uma arma não pode ser precisa e maciça.</p>
<p><strong>Pungente.</strong> Forjada várias vezes para adquirir o fio ou o equilíbrio perfeito, a arma fornece +2 nos testes de ataque. Pré-requisito: Certeira.</p>
<p><strong>Reforçada.</strong> Se for uma armadura, o item possui uma camada extra de tecido, malha mais grossa ou placas mais resistentes. Se for um escudo, possui uma chapa mais reforçada. O bônus na Defesa e a penalidade de armadura do item aumentam em 1. Um item não pode ser reforçado e delicado.</p>
<p><strong>Rúnica.</strong> O item possui runas mágicas que protegem contra magias ofensivas, conferindo resistência à magia +2. Pré-requisito: outra melhoria qualquer.</p>
<p><strong>Selada.</strong> A armadura foi forjada de forma a proteger todo o corpo do usuário, sem deixar brechas para nem mesmo um fio de cabelo! Esta melhoria fornece um bônus de +1 em testes de resistência, mas só pode ser aplicada em armaduras pesadas.</p>
<p><strong>Sob Medida.</strong> Embora muitas armaduras sejam feitas especificamente para um usuário, esta passou por um período longo de ajustes e aperfeiçoamento, encaixando-se com perfeição ao seu corpo. Reduz a penalidade por armadura em 2, mas apenas para o usuário específico (para outros, comporta-se como um item ajustado). Pré-requisito: Ajustada.</p>
<p><strong>Vigilante.</strong> O item usa parte de sua mana pessoal para gerar um campo que desvia ataques. Você recebe +5 na Defesa, mas perde -2 PM. Para ativar ou cancelar esse efeito você precisa realizar um ritual de 10min. Os PM não são recuperados automaticamente, precisando do tempo de descanso ou outras ações que possam recuperá-los.</p>
</section>
<section class="content-section" id="materiais-especiais">
<h2>Materiais Especiais</h2>
<p>Armas, armaduras, escudos e esotéricos podem ser feitos ou revestidos de um material especial. Para isso, o item precisa ter a melhoria material especial e você precisa pagar o custo extra do material escolhido, conforme a tabela 3-9.</p>
<p>Aço-Petrolífero</p>
<p>Esse metal sombrio, conhecido por sua dureza comparável à do aço, é extremamente cobiçado e exorbitantemente caro, disponível apenas através da exclusiva guilda de ferreiros de Mi’nérius que possuem a tecnologia necessária para sua extração nas camadas mais profundas de Pindorama. Os angáris mantêm em sigilo a origem desse material, porém suspeita-se que ele seja composto por vestígios fossilizados de civilizações extintas ao longo de eras.</p>
<p><strong>Arma.</strong> Uma criatura viva atingida sofre hemorragia. A condição de sangramento provocada pela arma é acumulativa — uma criatura atingida duas vezes perde 2d6 Pontos de Vida devido à hemorragia a cada rodada.</p>
<p><strong>Armadura e Escudo.</strong> Redução de Dano 5.</p>
<p><strong>Esotérico.</strong> Ao conjurar uma magia, é possível investir +2 PM para que ela ignore quaisquer reduções de dano, resistências e imunidades do alvo.</p>
<p>Pedra Lunar</p>
<p>Dizem que a própria Jaci presenteou seus adoradores com esse metal raro que caiu dos céus formando gigantescos vales em Pindorama. A Pedra lunar é um metal claro, brilhante e mais pesado que o aço.</p>
<p><strong>Arma.</strong> Aumenta o dano em um passo. Sobre a luz do luar você pode gastar 1PM e rolar duas vezes o dado, escolhendo o melhor resultado.</p>
<p><strong>Armadura e Escudo.</strong> Fornece redução de dano: armaduras leves e escudos, RD 2; armaduras pesadas, RD 5. Sobre a luz do luar esse bônus aumenta em +1.</p>
<p><strong>Esotérico.</strong> O custo da magia é reduzido em -1. Se lançar essa magia sobre a luz do luar você pode gastar 1 PM e rolar os dados 2 vezes e escolher o melhor resultado.</p>
<p>Escama de Boiroý</p>
<p>De tempos em tempos, os Boiroý realizam a troca de sua pele, assim como sua espécie antagônica, o boitatá. Estes seres habitam as regiões glaciais do Reino Pampas, onde expedições de aventureiros exploram os perigos naturais em busca desse material extraordinário. As escamas resultantes desse processo são cristalinas, quase transparentes e intensamente geladas, mantendo inalterada sua temperatura e emanando uma leve névoa devido ao seu frio intenso.</p>
<p><strong>Arma.</strong> Causa +2 pontos de dano por frio.</p>
<p><strong>Armadura e Escudo.</strong> Fornece resistência a fogo: armaduras leves e escudos, resistência 5; armaduras pesadas, resistência 10.</p>
<p><strong>Esotérico.</strong> Quando lança uma magia de frio que causa dano, você pode rolar novamente qualquer resultado 1 na rolagem de dano dela.</p>
<p>Lenha Ticum</p>
<p>Nos Domínios da Grande Aliança Verde, brota uma espécie de árvore conhecida como Ticum, cuja madeira é tão resistente quanto o aço e imbuída de propriedades mágicas. Somente armas de madeira — arcos, cajados, clavas, lanças, alabardas e tacapes —, escudos leves e artefatos esotéricos podem ser confeccionados com o Lenha Ticum. Os Floratas veneram estas árvores como os berços dos grandes heróis de sua raça, defendendo-as com devoção inabalável contra todo e qualquer explorador, chegando a construir verdadeiras fortificações ao redor dessas árvores raras.</p>
<p><strong>Arma.</strong> O Ataque realizado com uma arma de Lenha Ticum conta como mágico (acertando normalmente contra criaturas incorpóreas que possuem invulnerabilidade a dano físico). Além disso, habilidades ativadas ao se fazer um ataque ou usar a ação agredir têm seu custo em PM reduzido em –1.</p>
<p><strong>Armaduras e Escudos.</strong> Fornece resistência a magia e RD a dano magico +2.</p>
<p><strong>Esotéricos.</strong> Aumentam a quantidade de PM recuperada em descanso em +2.</p>
<p>Pau-Brasil</p>
<p>A árvore que deu nome ao império dos povos caucazis após o início de suas invasões é também uma fonte de poder mágico no bioma de Pindorama. O pau-brasil foi a primeira árvore a exalar a essência do Caos Primordial, chamando a atenção dos povos caucazis para a exploração de tal imensurável poder. Essa essência, ao ser extraída da árvore, tem a aparência de um líquido viscoso de cor roxa e brilho intenso, capaz de ser moldado por meio de artes mágicas. Ao ser solidificada, torna-se muito resistente e opaca, mantendo sua cor e tornando-se matéria-prima para a criação de armas, armaduras e artefatos esotéricos. Itens confeccionados a partir desse material são indubitavelmente poderosos, mas sua natureza caótica impõe consideráveis penalidades ao usuário.</p>
<p><strong>Arma.</strong> Causa +1d6 de dano extra. Porém, sempre que acerta um ataque com a arma, você e o oponente fazem um teste de vontade (CD = dano total causado). Quem falhar no teste, recebe um acúmulo de Caos. Quando o acúmulo de caos supera a sabedoria, o personagem entra em um estado de fúria gritando e atacando a criatura mais próxima (aliado ou inimigo) por 1d6 horas. Um acerto crítico com essa arma tem seu multiplicador aumentado em +1, mas você e o oponente recebem 1 acúmulo de caos automaticamente.</p>
<p><strong>Armadura e Escudo.</strong> Por sua essência instável sempre que receber dano em combate corpo a corpo seu inimigo sofre 1d6 pontos de dano de essência e tanto ele como o portador devem fazer o teste de vontade (CD = dano total causado) ou recebem um acumulo de caos. Se o ataque for realizado a distância, só o portador faz o teste.</p>
<p><strong>Esotérico.</strong> Você e todos os seus inimigos em alcance curto sofrem –2 em testes de resistência contra efeitos mágicos.</p>
<p>Itá</p>
<p><strong>Metal raro e valioso, o itá é prateado, reluzente e mais leve que aço.</strong> Itens de itá ocupam –1 espaço (mínimo 1).</p>
<p><strong>Arma.</strong> Aumenta sua margem de ameaça em 1. Por exemplo, uma espada longa de itá tem margem de ameaça 18-20.</p>
<p><strong>Armadura e Escudo.</strong> Tem sua penalidade de armadura diminuída em 2. Armaduras pesadas de itá permitem que você aplique até dois pontos de sua Destreza na Defesa.</p>
<p><strong>Esotérico.</strong> Permite que você pague +2 PM ao lançar uma magia para aumentar a CD dela em +2.</p>
<p>Alquimia Mística</p>
<p>Estes artefatos possuem propriedades que superam aquelas encontradas nos itens alquímicos comuns. São preciosos, encontrados apenas nas cidades mais avançadas ou nos lugares mais remotos, guardados nos refúgios secretos de sábios solitários.</p>
<p><strong>A CD para fabricar itens de alquimia mística é 25.</strong> Porém, para realizar tal proeza, um personagem deve possuir o poder de Mestre Alquimista.</p>
<p>Preparados.</p>
<p><strong>Ácido Concentrado.</strong> Similar a um ácido comum, porém, o alvo sofre 4d8 pontos de dano de ácido e fica vulnerável até o final da cena (Reflexos CD Des reduz à metade e evita a condição).</p>
<p><strong>Bálsamo Potente.</strong> Equiparado a um bálsamo restaurador, mas o alvo recupera 4d8 pontos de vida.</p>
<p><strong>Incenso Místico.</strong> Semelhante a um incenso normal, mas feito de substâncias raras. Queimá-lo e meditar por uma hora fornece 5 pontos de mana temporários.</p>
<p><strong>Suco Rochoso.</strong> Este líquido denso e cinzento aumenta a rigidez da pele do usuário. Fornece redução de dano 5 e diminui o deslocamento em –1,5m por uma cena.</p>
<p><strong>Suco Vermelho.</strong> Líquido cor de sangue que intensifica a agressividade e a capacidade de combate do usuário. Fornece +2 em testes de ataque e de resistência e –2 em perícias baseadas em Inteligência e Carisma por uma cena.</p>
<p><strong>Substrato de Mana.</strong> Semelhante à essência de mana, mas recupera 2d4 pontos de mana.</p>
<p><strong>Catalisador Concentrado.</strong> Todos os catalisadores existem em versões concentradas. Um catalisador concentrado gera o dobro do efeito comum, mas custa o triplo do preço. Por exemplo, musgo do mangue concentrado adiciona +4 de dano de frio a magias e custa M$ 135, terra de cemitério concentrada adiciona +2d6 de dano de trevas a magias e custa 90 PP.</p>
<p>Itens Mágicos</p>
<p>Itens mágicos são obtidos a partir dos tesouros saqueados de monstros vencidos ou descobertos em câmaras antigas perdidas. Esses artefatos conferem habilidades que um personagem dificilmente alcançaria de outra maneira, ou complementam de maneira extraordinária as capacidades de seu usuário.</p>
<p>A menos que você decida que sua campanha funcione de maneira diferente, a maioria dos itens mágicos é tão rara que não está disponível para compra. Itens comuns, como poções de cura, podem ser confeccionados por artífices ou conjuradores, no entanto, esse processo raramente é tão simples quanto entrar em uma loja e selecionar um item de uma prateleira. Nesse último caso um vendedor poderia muito comumente requerer um serviço em vez de moedas.</p>
<p>Em uma cidade de grande porte, com uma academia federal de ciência e magia ou um santuário sagrado de um deus, a compra e venda de itens mágicos pode ser possível. Ou mesmo se o seu mundo conta com uma considerável quantidade de aventureiros dedicados a recuperar itens mágicos antigos, a negociação desses itens pode ser mais comum. Mesmo assim, assemelha-se ao mercado de arte fina no mundo real, com leilões restritos a convidados e propenso a atrair ladrões.</p>
<p>Itens mágicos podem ser comuns para que os aventureiros possam comprá-los e vendê-los com algum esforço. Esses itens podem estar disponíveis em bazares ou casas de leilão em locais como em Feira do Agreste, a metrópole de Garoas, ou mesmo em cidades mais comuns onde artífices podem criar itens para uso militar ou de aventureiros, como fazem no Império do Brazil.</p>
<p>Personagens com habilidades específicas também podem produzir seus próprios itens mágicos, esses itens se dividem em duas categorias: de uso único (poções e pergaminhos) e permanentes (armas, armaduras/escudos e acessórios). Os itens mágicos permanentes, por sua vez, são classificados em menores, médios e maiores.</p>
<p>Utilizando Itens Mágicos</p>
<p>Tanto itens mundanos quanto mágicos seguem as mesmas regras para acúmulo de bônus e limites de uso e carga. Portanto, se um item mundano e um item mágico proporcionam um bônus na mesma característica, eles não se somam — utilize apenas o de maior benefício. Da mesma maneira, um personagem que utiliza dois itens mundanos e dois itens mágicos não receberá os benefícios de um quinto item em uso.</p>
<p><strong>Limites de Carga.</strong> Itens mágicos ocupam espaço conforme os itens mundanos. Uma espada longa flamejante, por exemplo, ocupa 1 espaço — equivalente ao ocupado por uma espada longa comum. Acessórios também ocupam 1 espaço, a menos que sua descrição indique o contrário. Poções e pergaminhos ocupam meio espaço.</p>
<p><strong>Ativação e Testes.</strong> Para itens mágicos com habilidades ativadas, é necessário identificá-los primeiro (veja a seguir). A menos que a descrição do item indique o contrário, ativar um item mágico é considerado uma ação padrão. Quanto aos itens que exigem testes de resistência para seus efeitos, o atributo usado para determinar a CD é especificado na descrição do item (para poções e pergaminhos, pode ser Inteligência, Sabedoria ou Carisma, a critério do usuário).</p>
<p><strong>Itens que Lançam Magias.</strong> O lançamento de magias a partir de itens não requer a pronúncia de palavras mágicas, gestos elaborados ou concentração, e as magias arcanas conjuradas por meio de itens não são afetadas por restrições devido ao uso de armaduras. O uso de aprimoramentos é permitido, mas requer o devido custeio.</p>
<p>Identificação de Itens Mágicos. Alguns itens mágicos podem parecer comuns, sem exibir características especiais. Outros são claramente encantados: emanam brilho, produzem zumbidos ou são adornados com runas reluzentes ou gemas cintilantes. Alguns itens possuem inscrições indicando suas capacidades; tais inscrições podem ser mágicas, alterando-se para um idioma que o usuário compreenda, ou exigir fluência em línguas exóticas (consulte a perícia Conhecimento). Outros itens podem não fornecer qualquer indício sobre seu funcionamento. Nesses casos, os poderes só se tornam evidentes mediante a identificação do item. Para realizar tal tarefa, utilize a perícia Misticismo — ou, em último caso, recorra à tentativa e erro. Sofrer um naufrágio próximo as fortes correntezas que circulam Noronhas é uma ótima oportunidade para testar aquele anel de Olho de Peixe funciona de verdade.</p>
<p>Angarium</p>
<p>Na narrativa ancestral, conta-se que o Deus da Forja deu origem ao Angarium a partir de sua própria essência, um metal de incomparável rigidez. Para moldar esse metal e conceder-lhe vida, ela subtraiu um pedaço da essência de seu irmão Kuaracyr, o Deus Sol, marcando o início de seu trabalho divino na forja que deu origem à raça Muiraquitã, seus filhos.</p>
<p>Em seguida ela instruiu seus filhos na arte de moldar esse metal exótico e precioso, e a reverencia-lo como uma dádiva divina por suas propriedades mágicas intrínsecas. Os habilidosos forjadores de Angarium afirmam não determinar a forma do item, mas, em vez disso, enaltecem a vida da própria deusa em seu interior e “escutam" do metal a forma necessária para que ele possa manifestar sua magia. Armas, armaduras, escudos e artefatos esotéricos podem ser meticulosamente confeccionados a partir do Angrarium.</p>
<p><strong>Arma.</strong> Recebem um bônus de +2 nos testes de ataque e nas rolagens de dano.</p>
<p><strong>Armadura e Escudo.</strong> Recebem um bônus de +2 na Defesa.</p>
<p><strong>Esotérico.</strong> Elevam a CD para resistir às suas magias em +1.</p>
<p><strong>Especial.</strong> O que torna esse material tão especial é que ele não conta apenas com esses benefícios (o que por si só já eleva seu valor),mas o fato dele carregar consigo uma magia específica, determinada no momento da sua fabricação. Ao empunhar a arma, escudo ou artefato esotérico, ou ao vestir a armadura, você pode lançar a magia contida no item, pagando o custo em PM, utilizando como atributo-chave Inteligência, Sabedoria ou Carisma, à sua escolha.</p>
<p>A adição de aprimoramentos da magia que não possuam pré-requisitos específicos é permitida, desde que estejam dentro do máximo de PM que você pode gastar em habilidades. Vale ressaltar que a conjuração da magia de um item de Angarium não requer palavras mágicas, gestos ou concentração, e as magias arcanas lançadas através do item não sofrem limitações pelo uso de armaduras.</p>
<p><strong>Embora os itens de Angarium sejam extraordinários, seu preço é mais elevado.</strong> O aumento no custo varia de acordo com o círculo da magia que ele contém: +M$ 30.000 para um item com uma magia de 1º ou 2º círculo, + M$ 60.000 para uma magia de 3º ou 4º círculo, e + M$90.000 para um item contendo uma magia de 5º círculo.</p>
<p>Devido à raridade desse material singular e ao fato de que o fabricante deve ter uma conexão com a deusa Angra (Ogum), itens de Angarium raramente são encontrados à venda. A confecção de um item de Angarium exige um teste de Ofício com uma CD aumentada em +10 em comparação a um item do mesmo tipo e o fabricante deve ser devoto de Angra (Ogum).</p>
<p>É possível que um mesmo item seja superior e confeccionado com Angarium. Os bônus de melhorias e de Angarium são cumulativos, assim como o aumento no preço devido à quantidade de melhorias e ao círculo da magia contida. Portanto, uma arma pungente de Angarium pode fornecer um impressionante bônus de +4 nos testes de ataque. Um item superior de Angarium, com 4 melhorias (M$ 18000, +20) e uma magia de 5º círculo (M$ 90000, +10), terá seu preço total aumentado em +M$ 108.000, e sua CD de fabricação será elevada em +30!</p>
</section>
<section class="content-section" id="pocoes-e-pergaminhos">
<h2>Poções e Pergaminhos</h2>
<p>Alguns herméticos, sacerdotes e xamãs dominam segredos antigos para "guardar" magias em poções ou pergaminhos, para que possam ser usados posteriormente por eles mesmos ou por outras pessoas. Cada poção ou pergaminho contém o efeito de uma magia. Quando é ativado, produz o efeito dessa magia e então some. Se a magia exigir teste de resistência, a CD é baseada em Int ou Sab, à escolha do usuário da poção.</p>
<p><strong>O preço de uma poção ou pergaminho é o custo em PM da magia ao quadrado (mínimo 1) x M$30.</strong> Assim, uma poção ou pergaminho contendo uma magia de 1º círculo (1 PM) custa M$30 PP (1² x M$30). Já um desses itens contendo uma magia de 2º círculo (3 PM) custa 270 PP (3² x 30 PP). A tabela ao lado mostra o preço calculado conforme o custo em PM.</p>
<p>Poções e pergaminhos são itens mágicos e são classificados conforme o círculo da magia que contêm: 1º ou 2º (item mágico menor), 3º ou 4º (médio) e 5º (maior).</p>
</section>
<section class="content-section" id="pocoes">
<h2>Poções</h2>
<p><strong>Uma poção é um líquido mágico armazenado em um frasco de vidro ou cerâmica.</strong> Poções podem conter apenas magias que tenham como alvo uma criatura ou objeto, ou que tenham efeito em área. Poções que afetem objetos também são chamadas de óleos e poções que gerem efeito em área também são chamadas de granadas.</p>
<p><strong>Ativação.</strong> Para ativar uma poção você deve bebê-la (a poção afeta quem ingeri-la). Isso exige uma ação padrão. Também é possível dar uma poção a uma criatura inconsciente gastando uma ação completa ou forçar uma criatura a bebê-la fazendo a manobra agarrar e então vencendo mais um teste de manobra.</p>
<p><strong>Ativar um óleo exige uma ação padrão para aplicá-lo no objeto que será afetado.</strong> Ativar uma granada exige uma ação padrão para arremessá-la em qualquer ponto em alcance curto (o centro do efeito da magia é o ponto onde a granada foi arremessada).</p>
</section>
<section class="content-section" id="pergaminhos">
<h2>Pergaminhos</h2>
<p><strong>Um pergaminho mágico é uma folha grossa de papel, papiro, couro ou outros materiais.</strong> Pergaminhos podem conter qualquer magia. Quando as palavras escritas nele são pronunciadas, a magia é ativada e o pergaminho se desfaz em cinzas.</p>
<p><strong>Ativação.</strong> Para ativar um pergaminho você deve lê-lo em voz alta. Isso exige uma ação padrão ou a ação necessária para lançar a magia, o que for maior. Para ler um pergaminho, você deve conhecer a magia escrita nele ou passar em um teste de Misticismo (CD 20 + custo em PM da magia).</p>
<p>Quando ativa um pergaminho, você toma quaisquer decisões exigidas pela magia, como se a tivesse lançado, e aplica habilidades que se aplicariam às suas próprias magias. Caso conheça a magia, pode aplicar aprimoramentos nela, pagando o custo em pontos de mana deles (você paga apenas o custo dos aprimoramentos, não o custo básico da magia).</p>
<p><strong>Fabricando.</strong> Para fabricar uma poção ou pergaminho, escolha uma magia que você conheça. Essa será a magia que o item irá conter (lembre-se que poções podem conter apenas magias que tenham como alvo uma criatura ou objeto ou que tenham efeito em área). A CD para fabricá-lo é 20 + o custo em PM da magia.</p>
<p>Quando fabrica uma poção, você pode aplicar aprimoramentos nela, até seu limite de gasto de PM, como se estivesse lançando a magia (o preço e a CD do teste de Ofício são ajustados de acordo). Quando fabrica um pergaminho, você não pode aplicar aprimoramentos (mas pode fazer isso ao ativá-lo).</p>
<section class="content-section" id="itens-permanentes">
<h2>Itens Permanentes</h2>
<p>Armas e armaduras mágicas podem ser dotadas de encantamentos ou serem específicas, enquanto acessórios sempre apresentam características específicas.</p>
<p><strong>Itens Encantados.</strong> Estes operam conforme os itens superiores (consulte a página 164), mas, em vez de melhorias, exibem encantos. Um item mágico menor possui um encanto, um médio possui dois, e um item mágico maior possui três encantos. O custo e a CD para o teste de Ofício aumentam proporcionalmente ao número de encantos (consulte a tabela abaixo). Bônus provenientes de encantos não se acumulam.</p>
<p><strong>É possível que um mesmo item seja tanto superior quanto encantado.</strong> Some os modificadores de custo e CD para o teste de Ofício, bem como os bônus proporcionados por melhorias e encantos, para determinar o bônus do item.</p>
<p>Por exemplo, uma espada longa com um encanto tem o custo de T$ 18.015. A fabricação requer um investimento de T$ 6.005 e um teste de Ofício com CD 30. Já uma espada longa com quatro melhorias e três encantos (o máximo possível) tem um custo de T$ 90.015 (T$ 15 pela espada + T$ 18.000 pelas quatro melhorias, mais T$ 72.000 pelos três encantos). Sua fabricação exige um investimento de T$ 30.005 e um teste de Ofício com CD 60. Somente os artífices mais habilidosos são capazes de forjar uma arma desse calibre!</p>
<p><strong>Itens Específicos.</strong> Seguem as regras de fabricação de itens de Ofício, e o preço de cada item é listado nas tabelas abaixo. A CD para o teste de Ofício é determinada pela categoria do item: CD 30 para itens menores, CD 40 para médios e CD 50 para itens maiores. Além disso, a perícia utilizada é determinada pelo tipo de item: Ofício (armeiro) para armas e armaduras, e Ofício (artesão) para acessórios.</p>
<p>Conforme a orientação do mestre, outros Ofícios podem ser empregados para itens específicos, como Ofício (joalheiro) para um anel. Importante notar que itens específicos não podem receber encantos. Todas as armas e armaduras específicas deste livro são consideradas itens maiores.</p>
<p>Custo em Pontos de Mana. Para forjar um item mágico permanente, o personagem precisa doar uma parte de sua energia vital: 1 PM para itens menores, 2 PM para itens médios e 3 PM para itens maiores. Essa essência é o que dá vida ao item. Os pontos de mana não podem ser recuperados. Porém, se o personagem destruir um item mágico permanente que ele mesmo criou, recupera os PM ofertados naquele item. A critério do mestre, outras coisas podem substituir os pontos de mana, como ingredientes exóticos. Mas encontrar algo assim é sempre um desafio — quem sabe o motivo de uma aventura!</p>
<p>Destruindo Itens Mágicos</p>
<p>Para saber as propriedades de um item mágico, consulte a seção “Destruindo Objetos”, do Capítulo 5, para as propriedades de um item comum do mesmo tipo. Itens mágicos permanentes ganham um bônus em PV e RD de acordo com sua categoria: +10 para itens menores, +20 para itens médios e +40 para itens maiores. Por exemplo, uma espada longa (normalmente PV 5 e RD 10) de categoria maior tem PV 45 e RD 50. Um item mágico que não esteja em uso faz seus próprios testes de resistência. O bônus varia com a categoria: +5 para itens menores, +10 para itens médios e +20 para itens maiores. Se estiver sendo usado, pode optar pelo seu bônus ou o do usuário.</p>
<p>Armas Mágicas</p>
<p>Seja forjada com um propósito há muito esquecido ou criada para honrar os mais nobres ideais revolucionários, as armas mágicas são almejadas por muitos aventureiros. Elas são consideradas itens encantados e podem apresentar um (item menor), dois (item médio) ou três (item superior) dos encantamentos apresentados na lista abaixo.</p>
<p>Encantos</p>
<p><strong>Ameaçadora.</strong> A arma tem maior chance de acertar pontos vitais. A margem de ameaça dobra. Exemplo: um florete com margem de ameaça 18 (crítico em 20, 19 e 18) ao receber esse aprimoramento fica com a margem de ameaça 15 (crítico em 20, 19, 18, 17, 16 e 15).</p>
<p><strong>Anticriatura.</strong> Esta arma é letal contra um tipo específico de espécie ou raça específica. Uma vez por rodada, ao atacar uma criatura desse tipo, você pode gastar 2 PM. Se o fizer e acertar o ataque, inflige +4d8 de dano. Para determinar aleatoriamente o tipo de criatura, role 1d6: 1) animal; 2) construto; 3) espírito; 4) monstro; 5) morto-vivo; 6) uma raça de humanoides.</p>
<p><strong>Arremesso.</strong> A arma pode ser lançada em alcance curto. Caso já pudesse ser lançada, seu alcance aumenta em uma categoria. Após o ataque, se estiver livre, a arma retorna voando para você ou simplesmente aparece em sua mão. Segura-la novamente é uma reação.</p>
<p><strong>Assassina.</strong> A arma potencializa os dados de dano extra de um ataque furtivo para d8. Além disso, quando faz um Ataque Furtivo, você pode gastar 2 PM. Se fizer isso, pode repetir quaisquer resultados 1 nesses dados de dano extra.</p>
<p><strong>Caçadora.</strong> A arma segue o alvo, ignorando penalidades por camuflagem leve e total e por cobertura leve. Caso a arma seja de ataque à distância, seu alcance também aumenta em uma categoria.</p>
<p><strong>Congelante.</strong> A arma causa +1d6 de dano de frio. Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso e acertar o ataque, a vítima fica imobilizada por uma rodada. Uma arma congelante é envolta por uma camada de gelo e névoa.</p>
<p><strong>Conjuradora.</strong> Um conjurador pode infundir na arma uma magia que tenha como alvo uma criatura ou que afete uma área. A magia não surge na hora; em vez disso, fica armazenada no item. Quando acerta um ataque com a arma, você pode liberar a magia armazenada como uma ação livre e sem pagar seu custo. Ela tem como alvo (ou como centro de sua área) a criatura ou ponto atingido pelo ataque. Uma vez que a magia seja liberada, outra pode ser infundida.</p>
<p><strong>Corrosiva.</strong> A arma causa +1d6 de dano de ácido. Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso e acertar o ataque, a vítima sofre 4d4 pontos de dano de ácido na próxima rodada. Uma arma corrosiva solta vapores e pinga um líquido venenoso.</p>
<p><strong>Dançarina.</strong> Você pode gastar uma ação de movimento e 1 PM para fazer a arma levitar e atacar uma criatura em alcance curto a sua escolha, com as mesmas estatísticas que teria se você a estivesse empunhando. Este efeito tem duração sustentada; se parar de sustentá-lo, a arma cai no chão.</p>
<p><strong>Defensora.</strong> A arma se move para bloquear ataques contra você. Você recebe +2 na Defesa. Destruidora. Se usada contra construtos e objetos (com a manobra quebrar), a arma fornece +2 no teste de ataque e causa +2d8 de dano.</p>
<p><strong>Dilacerante.</strong> A arma provoca ferimentos graves. Quando faz um acerto crítico com a arma, você causa +10 pontos de dano.</p>
<p><strong>Drenante.</strong> Quando você faz um acerto crítico em uma criatura viva, a criatura fica fraca e você ganha 2d10 pontos de vida temporários. Uma arma drenante emite um brilho púrpura.</p>
<p><strong>Elétrica.</strong> A arma causa +1d6 de dano de eletricidade. Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso e acertar o ataque, um raio atinge outra criatura em alcance curto, causando 3d8 pontos de dano de eletricidade. Uma arma elétrica solta faíscas e é coberta de arcos voltaicos.</p>
<p><strong>Energética.</strong> A arma tem sua parte perigosa (a lâmina de uma espada, a ponta de uma lança…) transformada em magia pura. Ela fornece +4 em testes de ataque, ignora 20 pontos de redução de dano, converte todo o dano causado para essência e irradia luz como uma tocha. Pré-requisito: formidável.</p>
<p><strong>Excruciante.</strong> A arma inflige dor insuportável. Uma criatura viva atingida fica fraca. Se já estiver fraca, fica debilitada (a condição máxima que esta arma pode causar).</p>
<p><strong>Flamejante.</strong> A arma causa +1d6 de dano de fogo. Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso, em vez do ataque normal você lança uma bola de fogo contra um alvo em alcance médio. O alvo sofre 6d6 pontos de dano. Um teste de Reflexos (CD For ou Des, o que for melhor) diminui pela metade. Uma arma flamejante irradia chamas como uma tocha.</p>
<p><strong>Formidável.</strong> Esta arma confere +2 em testes de ataque e rolagens de dano, proporcionando golpes certeiros e letais.</p>
<p><strong>Lancinante.</strong> A arma provoca ferimentos fatais. Quando faz um acerto crítico com a arma, você causa +10 pontos de dano ou, além de multiplicar os dados de dano, multiplica também quaisquer bônus numéricos, o que preferir. Este efeito substitui o efeito de dilacerante. Pré-requisito: dilacerante.</p>
<p><strong>Magnífica.</strong> A arma é encantada para golpear com perfeição. Ela fornece +4 em testes de ataque e rolagens de dano. Pré-requisito: formidável.</p>
<p><strong>Piedosa.</strong> A arma causa +1d8 de dano, mas todo o dano causado é não letal. Você pode gastar 1 PM para ligar e desligar este encanto.</p>
<p><strong>Profana.</strong> A arma causa +2d8 de dano contra criaturas boas (a critério do mestre). Uma arma profana emite luz vermelha pulsante.</p>
<p><strong>Sagrada.</strong> A arma causa +2d8 de dano contra criaturas más (a critério do mestre). Uma arma sagrada emite uma leve luz branca.</p>
<p><strong>Sanguinária.</strong> Uma criatura viva atingida fica sangrando. Dano de sangramento causado pela arma é cumulativo — uma criatura atingida duas vezes perde 2d6 PV por sangramento por rodada.</p>
<p><strong>Trovejante.</strong> A arma solta um estrondo a cada golpe. Quando você faz um acerto crítico, a vítima fica atordoada por uma rodada (só uma vez por cena; Fort CD For ou Des, o que for melhor, evita).</p>
<p><strong>Tumular.</strong> A arma causa +1d8 de dano de trevas. Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso, o bônus de dano aumenta para +2d8, mas você perde 1d8 pontos de vida. Uma arma tumular suga o calor ao redor.</p>
<p><strong>Veloz.</strong> Você recebe a habilidade Ataque Extra, do guerreiro, mas só pode usá-la com esta arma. Se já a possui, em vez disso, o custo para usá-la com esta arma diminui em –1 PM.</p>
<p><strong>Venenosa.</strong> Uma vez por rodada, quando ataca, você pode gastar 2 PM. Se fizer isso e acertar o ataque, a vítima fica envenenada, perdendo 1d12 pontos de vida por rodada durante 3 rodadas. Uma arma venenosa escorre um líquido verde e pegajoso.</p>
<p>Armas Específicas</p>

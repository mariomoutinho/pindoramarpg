<p><strong>Em meio às armas e armaduras, há uma gama de objetos essenciais para os aventureiros.</strong> Os itens gerais se desdobram em nove categorias distintas: equipamento de aventura, ferramentas, vestuário, artefatos esotéricos, elixires alquímicos, provisões alimentares, companheiros animais, meios de locomoção e serviços variados.</p>
<p>Alguns desses itens demandam ser empunhados para desencadear sua utilidade, enquanto outros requerem ser habilmente vestidos (consulte "Limites de Uso" na página 141 para as diretrizes sobre o uso adequado de tais itens). Aqueles que concedem a oportunidade de um teste de resistência seguem os mesmos parâmetros dos testes de resistência relacionados às habilidades (consulte a página 227 para mais informações).</p>

<p><strong>Sangue de Oxóssi.</strong> Um líquido pálido que se pinga nos olhos, capaz de aguçar a visão e a precisão. Uma receita peculiar, muito usada por pescadores e caçadores, que melhora a capacidade de acertar o alvo. Concede +2 em testes de Pontaria até o fim da cena. Ressaca.</p>

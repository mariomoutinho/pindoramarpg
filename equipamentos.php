<section class="content-section" id="equipamento-de-aventura">
<h2>Equipamento de Aventura</h2>
<p><strong>Ferramentas indispensáveis para desbravadores de masmorras.</strong> A CD para fabricar qualquer um destes itens é 15.</p>
<p><strong>Água Benta.</strong> Destilada através do encanto da magia Abençoar Alimentos, esta água sagrada representa um recurso formidável na batalha contra as forças malignas. Ao usar a água sagrada, você gasta uma ação padrão para escolher uma criatura morta-viva, demônio ou espirito das trevas dentro do alcance curto (a água sagrada não afeta outras criaturas). O alvo sofre 2d10 pontos de dano de luz (Reflexos CD Sab reduz à metade).</p>
<p>Algemas. Um par de algemas projetadas para criaturas de porte Médio. Imobilizar uma criatura que não está indefesa requer que você empunhe as algemas, agarre o alvo (consulte "Manobras de Combate" no Capítulo 5) e vença um novo teste de agarrar. Você pode prender ambos os pulsos da pessoa (–5 em testes que exigem o uso das mãos, impedindo a conjuração) ou um dos pulsos dela em um objeto imóvel adjacente, se disponível, para evitar seu movimento. Escapar das algemas demanda uma ação completa e um teste de Acrobacia contra CD 30 ou de Força contra CD 25 — a menos que você tenha as chaves...</p>
<p><strong>Arpéu.</strong> Um gancho de aço amarrado na extremidade de uma corda, perfeito para fixar em muros, janelas, ou parapeitos de edifícios. Prender um arpéu requer um teste de Pontaria (CD 15). Subir um muro com a assistência de uma corda proporciona um bônus de +5 no teste de Atletismo.</p>
<p><strong>Bandoleira de Poções.</strong> Um cinto de couro com bolsos projetados para acomodar pequenos frascos. Se estiver utilizando uma bandoleira, pode sacar itens alquímicos e poções como uma ação livre.</p>
<p><strong>Barraca.</strong> Essa tenda de lona equivale a um saco de dormir para duas pessoas e confere um bônus de +2 em testes de Sobrevivência para acampar.</p>
<p><strong>Corda.</strong> Um rolo contendo 10 metros de corda de cânhamo, similar àquela utilizada em navios. Possui diversas aplicações, como auxiliar na descida de buracos ou paredes (+5 em testes de Atletismo nessas situações), amarrar pessoas, entre outras. Amarrar um nó firme ou especial (por exemplo, capaz de deslizar ou desfazer-se com um puxão) requer um teste de Destreza (CD 15). Romper a corda exige 2 pontos de dano cortante ou uma ação padrão e um teste de Força (CD 20).</p>
<p><strong>Espelho.</strong> Um pequeno espelho multifuncional, útil para observar cantos, enviar sinais luminosos e, é claro, garantir que você esteja apresentável.</p>
<p><strong>Lampião.</strong> Um cilindro com uma alça e duas aberturas. Uma chama alimentada por óleo é acesa dentro do cilindro, e abrir uma das aberturas permite que a luz se propague. Acender um lampião demanda uma ação padrão, e sua luz ilumina um raio de 15 metros. Recarregar um lampião com óleo também exige uma ação padrão, e ele permanece aceso por toda uma cena.</p>
<p><strong>Mochila.</strong> Um saco de lona com alças para carregar nas costas, não contabilizado como item vestível.</p>
<p><strong>Mochila de Aventureiro.</strong> Confeccionada em couro resistente e repleta de bolsos para organizar equipamentos. Vestir uma mochila de aventureiro aumenta a capacidade de carga em 2 espaços (a mochila em si não ocupa um espaço).</p>
<p><strong>Óleo.</strong> Um frasco contendo óleo inflamável para lampião. Pode ser lançado em uma criatura em alcance curto com uma ação padrão. Se a criatura sofrer dano de fogo até o final do seu próximo turno, ela sofre 1d6 pontos de dano adicional e fica em chamas.</p>
<p><strong>Organizador de Pergaminhos.</strong> Um estojo de madeira ou couro rígido. Vestir um organizador de pergaminhos permite que você sacuda pergaminhos como uma ação livre.</p>
<p><strong>Pé de Cabra.</strong> Uma barra de ferro que concede um bônus de +5 em testes de Força para abrir portas, janelas e baús fechados. Pode também ser utilizada como arma, com as estatísticas de uma clava.</p>
<p><strong>Saco de Dormir.</strong> Um leito compacto com uma coberta suficientemente fina para ser enrolada e amarrada, mostrando-se particularmente valioso para aventureiros que frequentemente desconhecem seu destino para pernoitar. Descansar ao ar livre sem a presença de um acampamento e um saco de dormir resulta em uma redução na recuperação de PV e PM (conferir página 106 para mais detalhes).</p>
<p><strong>Símbolo Sagrado.</strong> Um medalhão confeccionado em madeira ou metal, adornado com o símbolo de uma divindade. Se portado (comumente em uma corrente ao redor do pescoço) ou empunhado, o símbolo sagrado de uma divindade adorada confere um bônus de +1 em testes de resistência.</p>
<p><strong>Tocha.</strong> Um bastão de madeira com a extremidade impregnada de algum combustível (geralmente trapos embebidos em parafina). Acender uma tocha requer uma ação padrão. Ilumina um raio de 9 metros e permanece acesa por toda uma cena. Pode ser utilizada como uma arma simples leve (dano 1d4 de impacto mais 1 de fogo, crítico x2).</p>
<p><strong>Vara de Madeira.</strong> Uma haste de 3 metros de comprimento. Útil para alcançar locais distantes, porém frágil demais para servir como arma.</p>
</section>
<section class="content-section" id="ferramentas">
<h2>Ferramentas</h2>
<p>Itens nesta categoria impactam testes de perícia, eliminando penalidades ou proporcionando bônus. A CD para fabricar qualquer ferramenta é 20.</p>
<p><strong>Afoxé.</strong> Um tipo de chocalho usado em manifestações culturais e rituais. Um boêmio que empunha este item aumenta a CD para resistir às magias de Encantamento lançadas por ele em +2. É reconhecido como um instrumento musical.</p>
<p><strong>Atabaque.</strong> Um tambor grande construído em madeiras resistentes, com batidas profundas e impactantes, utilizado em rodas de samba e ritmos zambi. Quando empunhado por um boêmio, o custo das magias que causam dano é reduzido em -1 PM.</p>
<p><strong>Berimbau.</strong> Um instrumento de percussão de origem zambi, composto por uma vara, uma cabaça e um arame. Enquanto segura este item, a Inspiração do Boêmio concede um bônus de +1 na Defesa a todos os aliados que passarem em um teste de Atuação (CD 12). Os aliados que não passarem no teste podem refaze-lo no inicio de seu próximo turno.</p>
<p><strong>Bombo Leguero.</strong> Um instrumento musical típico dos Zambi, capaz de produzir sons graves e retumbantes que podem ser ouvidos até 2 léguas de distância (daí o nome leguero). Enquanto segura este item, o alcance da habilidade Inspiração e qualquer Performance de Boêmio é dobrado. Ele é classificado como um instrumento musical.</p>
<p><strong>Coleção de Livros.</strong> Uma pequena compilação de tomos e tratados sobre um tema específico. Concede +1 em testes de Conhecimento, Guerra, Misticismo, Nobreza ou Religião (determinado no momento da compra ou fabricação).</p>
<p><strong>Equipamento de Viagem.</strong> Um saco de lona contendo instrumentos essenciais para sobreviver em regiões selvagens, como pederneira (para fazer fogo), panelas e talheres para cozinhar, anzol e linha para pesca, e uma pequena pá. Um personagem sem este item sofre uma penalidade de –5 em testes de Sobrevivência para acampamento. Não inclui saco de dormir ou barraca.</p>
<p><strong>Estojo de Disfarces.</strong> Um conjunto de cosméticos, tintas para cabelo e algumas próteses simples (como bigodes e narizes falsos). Um personagem sem este item sofre uma penalidade de –5 em testes de Enganação para disfarce.</p>
<p><strong>Flauta de Bambu.</strong> Um instrumento delicado de bambu, adornado com penas, runas e pequenas gemas místicas. Um boêmio que empunha este item aumenta a CD para resistir às magias lançadas por ele em +1. É reconhecido como um instrumento musical.</p>
<p><strong>Gazua.</strong> Uma barra fina de ferro, com a ponta torta ou em forma de gancho. Um personagem sem este item sofre uma penalidade de –5 em testes de Ladinagem para abrir fechaduras.</p>
<p><strong>Instrumentos de “Ofício”: Existe uma versão deste item para cada perícia de Ofício.</strong> Por exemplo, martelo, pinças e bigornas para Ofício (ferreiro), agulhas, linhas e tesouras para Ofício (alfaiate), e assim por diante. Um personagem sem os instrumentos de seu Ofício sofre uma penalidade de –5 nessa perícia.</p>
<p><strong>Instrumento Musical.</strong> Um instrumento típico, como uma cuica, sanfona ou rabeca. Consulte o quadro acima para as regras deste item.</p>
<p><strong>Luneta.</strong> Este instrumento valioso consiste em um cilindro metálico com duas lentes. Proporciona +5 em testes de Percepção para observar objetos em alcance longo ou além.</p>
<p><strong>Maleta de Medicamentos.</strong> Caixa de madeira contendo ervas, unguentos, bandagens e outros materiais úteis. Um personagem sem este item sofre uma penalidade de –5 em Cura.</p>
<p><strong>Pífano Florata.</strong> Confeccionado com madeira de alta qualidade e artesanato delicado, este pífano emite notas vivas e emocionantes. Enquanto segura esse item, você pode usar a habilidade Inspiração como uma ação de movimento. Ele é reconhecido como um instrumento musical.</p>
<p><strong>Sela.</strong> Uma peça de couro e pelego colocada sobre o lombo da montaria, incluindo arreios para conduzir o animal. Um personagem montado em uma montaria sem sela sofre uma penalidade de –5 em testes de Cavalgar. Usada no animal, a sela não ocupa espaço de carga do personagem.</p>
<p><strong>Sanfona.</strong> Com seu fole expansível e teclas distintas, a sanfona é capaz de evocar uma variedade de emoções, desde as notas animadas do forró até as melodias mais suaves e contemplativas. Instrumento comum entre os candangos que quando em punhada por boêmios concede um aprimoramento a magias que ultrapassa o limite normal de PM gastos (você ainda deve pagar o custo do aprimoramento em PM).</p>
</section>
<section class="content-section" id="vestuario">
<h2>Vestuário</h2>
<p><strong>Todos os itens nesta categoria devem ser vestidos para proporcionar seus benefícios.</strong> A CD para fabricar qualquer peça de vestuário é 20.</p>
<p>Andrajos de Aldeão. Vestimenta típica de camponeses, composta por uma camisa larga e calças soltas ou blusa e saia, sem incluir botas — os mais desfavorecidos frequentemente caminham descalços. Concede +2 em testes de Investigação para interrogar (já que ninguém se importa com o que um aldeão escuta) e, se você possuir o poder Aparência Inofensiva, a CD para resistir a ele aumenta em +2. Entretanto, impõe uma penalidade de –2 em perícias baseadas em Carisma contra pessoas que valorizam a classe social.</p>
<p><strong>Bandana.</strong> Um lenço tipicamente usado por bandidos e piratas. Oferece +1 em Intimidação.</p>
<p><strong>Botas Reforçadas.</strong> Botas altas e robustas que protegem contra perigos do terreno. Aumentam o deslocamento em +1,5m se ele for reduzido por terreno difícil (após a redução).</p>
<p><strong>Camisa Bufante.</strong> Blusa colorida com mangas e golas longas e encrespadas. Confere +1 em Atuação.</p>

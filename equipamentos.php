<p><strong>Gema Elemental.</strong> Você pode lançar Conjurar Elemental sem pagar seu custo em PM.</p>
<p><strong>Lanterna da Revelação.</strong> Este item funciona como um lampião normal, mas sua luz revela todas as criaturas e objetos invisíveis no alcance.</p>
<p><strong>Luvas da Delicadeza.</strong> Estas luvas de tecido fino permitem manipulação delicada. Você recebe +1 em Destreza (somente após um dia de uso).</p>

<p><strong>Martelo de Mi’nérius.</strong> Este martelo de guerra formidável é feito de pedra e aço. Quando empunhado por um angári, adquire o encanto arremesso e aumenta seu dano em +1d8 (ou +2d8 se usado contra criaturas Grandes ou maiores).</p>
<p><strong>Punhal de Kiantomerê.</strong> Esta adaga assassina formidável venenosa tem lâmina negra e serpenteada. Você pode gastar uma ação padrão e 2 PM para transformar o Punhal de Kiantomerê em um objeto inofensivo de tamanho similar, como uma colher ou pena. Nenhuma magia é capaz de revelar essa transformação. Transformar o punhal em arma é uma ação livre.</p>
<p><strong>Vingadora Sagrada.</strong> Esta espada longa formidável revela todo o seu poder apenas quando empunhada por um inquisidor. Se você for um inquisidor, recebe +5 em testes de ataque e rolagens de dano, o custo de seu Golpe de Juramento é reduzido em –1 PM e você e seus aliados em alcance curto recebem resistência a magia +5.</p>
<p>Armaduras &amp; Escudos</p>
<p>Em um mundo repleto de perigos sobrenaturais e desafios épicos, a proteção oferecida por armaduras e escudos transcende a simples barreira física. Imbuídos com poderes arcanos e encantamentos misteriosos, elevam a defesa dos aventureiros a um novo patamar. Assim como as armas, as armaduras e escudos mágicos também são considerados itens encantados e podem apresentar um (item menor), dois (item médio) ou três (item superior) dos encantamentos apresentados na lista abaixo.</p>
<p>Encantos de Armaduras e Escudos</p>
<p><strong>Abascanto.</strong> Você recebe resistência a magia +5.</p>
<p><strong>Abençoado.</strong> Você recebe redução de trevas 10 e +5 em testes de resistência contra efeitos de necromancia. Um item abençoado é adornado com gravuras de símbolos sagrados de deuses de energia expansiva.</p>
<p><strong>Acrobático.</strong> Você recebe +5 em Acrobacia e ignora a penalidade de armadura do item para testes dessa perícia. Alado. Você pode gastar 2 PM para fazer asas brotarem de suas costas e receber deslocamento de voo 12m com duração sustentada.</p>
<p><strong>Animado.</strong> Você pode gastar uma ação de movimento e 1 PM para fazer o escudo pairar ao seu redor até o fim da cena. Você recebe o mesmo bônus na Defesa que receberia se estivesse empunhando o escudo, mas fica com as duas mãos livres. Você só pode ser protegido por um escudo ao mesmo tempo.</p>
<p><strong>Assustador.</strong> Você pode gastar uma ação de movimento e 2 PM para provocar uma onda de medo. Inimigos em alcance curto devem passar num teste de Vontade (CD Car) ou ficarão amedrontados até o fim da cena. Um item assustador possui marcas de sangue, ossos pendurados e outras decorações macabras.</p>
<p><strong>Cáustica.</strong> Você recebe redução de ácido 10 e pode gastar uma ação de movimento e 2 PM para fazer o item pingar ácido. Se fizer isso, seus ataques causam +1d4 de dano de ácido até o fim da cena.</p>
<p><strong>Defensor.</strong> O item é encantado para bloquear golpes. O bônus na Defesa do item aumenta em +2.</p>
<p><strong>Escorregadio.</strong> Você recebe +10 em testes de Acrobacia para escapar e em testes de manobra contra agarrar. Um item escorregadio parece estar sempre coberto de óleo levemente viscoso.</p>
<p><strong>Esmagador.</strong> Este escudo fornece +2 em ataques e dano e tem seu dano aumentado em um passo.</p>
<p><strong>Fantasmagórico.</strong> Você pode lançar a magia Manto de Sombras. Um item fantasmagórico é cinzento e nebuloso.</p>
<p><strong>Fortificado.</strong> Você recebe 25% de chance (para escudos) e 50% de chance (para armaduras) de ignorar o dano extra de acertos críticos e ataques furtivos.</p>
<p><strong>Gélido.</strong> Você recebe redução de frio 10 e pode gastar uma ação de movimento e 2 PM para se envolver de gelo até o fim da cena. Se fizer isso, recebe 10 PV temporários. Um item gélido é azulado e gelado ao toque.</p>
<p><strong>Guardião.</strong> O item emite um campo de força que desvia ataques. O bônus na Defesa do item aumenta em +4. Pré-requisito: defensor.</p>
<p><strong>Hipnótico.</strong> Você pode gastar uma ação padrão e 3 PM para emitir luzes coloridas. Inimigos em alcance curto devem passar num teste de Vontade (CD Car) ou ficarão encantados por 1d6 rodadas. O efeito termina se qualquer criatura afetada for atacada. Um item hipnótico é vistoso e colorido.</p>
<p><strong>Ilusório.</strong> Você pode gastar uma ação de movimento e 1 PM para fazer o item adquirir a aparência de uma roupa comum, mas mantendo suas propriedades (bônus na Defesa, penalidade de armadura…). A magia Visão da Verdade revela o item disfarçado.</p>
<p><strong>Incandescente.</strong> Você recebe redução de fogo 10 e pode gastar uma ação de movimento e 2 PM para fazer o item soltar labaredas até o fim da cena. Se fizer isso, no início de cada um de seus turnos você causa 1d6 pontos de dano de fogo em todas as criaturas adjacentes. Um item incandescente é avermelhado e ardente ao toque.</p>
<p><strong>Invulnerável.</strong> Você recebe redução de dano 2 (para escudos) ou 5 (para armaduras).</p>
<p><strong>Opaco.</strong> Você recebe redução de ácido, eletricidade, fogo e frio 10. Um item opaco parece sem cor, totalmente comum e sem graça.</p>
<p><strong>Protetor.</strong> Você recebe +2 em testes de resistência.</p>
<p><strong>Refletor.</strong> Uma vez por rodada, quando você é alvo de uma magia, pode gastar PM igual ao custo dela para refleti-la de volta ao conjurador. As características da magia (efeitos, CD…) se mantêm, mas você toma qualquer decisão exigida por ela. Um item refletor parece espelhado.</p>
